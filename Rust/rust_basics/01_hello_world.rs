fn part_five_macro_reference() {
    println!("============================================================");
    println!("第五部分：宏卡片速查");
    println!("============================================================");

    /*
    【宏1：println!】

    语法：println!(格式字符串, 参数...)
    参数：
      - 格式字符串: 必需，包含文本和{}占位符
      - 参数: 可选，填充占位符的值
    返回：无返回值（单元类型()）

    示例：
    ```rust
    println!("Hello");
    println!("Hello, {}!", "Rust");
    println!("{} + {} = {}", 1, 2, 3);
    ```

    ────────────────────────────────────

    【宏2：print!】

    语法：print!(格式字符串, 参数...)
    参数：
      - 格式字符串: 必需
      - 参数: 可选
    返回：无返回值
    区别：不换行（println!会换行）

    示例：
    ```rust
    print!("Hello, ");
    print!("World!");  // 输出：Hello, World!
    ```

    ────────────────────────────────────

    【宏3：eprintln!】

    语法：eprintln!(格式字符串, 参数...)
    参数：
      - 格式字符串: 必需
      - 参数: 可选
    返回：无返回值
    区别：输出到标准错误（stderr）而不是标准输出（stdout）

    示例：
    ```rust
    eprintln!("Error: file not found");
    ```
    */
}


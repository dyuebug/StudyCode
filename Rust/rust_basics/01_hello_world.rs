fn part_two_basic_practice() {
    println!("============================================================");
    println!("第二部分：基础操作实践");
    println!("============================================================");

    // --------------------------------------------
    // 2.1 示例1：最简单的Hello World
    // --------------------------------------------

    println!("2.1 示例1：最简单的Hello World");
    println!("------------------------------------------------------------");

    // 打印Hello World
    // println!是宏，用于输出文本到控制台
    println!("Hello, World!");

    // --------------------------------------------
    // 2.2 示例2：带变量的输出
    // --------------------------------------------

    println!("\n2.2 示例2：带变量的输出");
    println!("------------------------------------------------------------");

    // 声明变量：let关键字
    // name是变量名，"Rust"是字符串字面量
    let name = "Rust";

    // 使用占位符插入变量
    // {}会被name的值替换
    println!("Hello, {}!", name);

    // --------------------------------------------
    // 2.3 示例3：多个占位符
    // --------------------------------------------

    println!("\n2.3 示例3：多个占位符");
    println!("------------------------------------------------------------");

    // 声明多个变量
    let language = "Rust";
    let year = 2015;  // Rust 1.0发布年份

    // 多个{}按顺序对应参数
    // 第一个{}对应language，第二个{}对应year
    println!("{} was released in {}", language, year);

    // --------------------------------------------
    // 2.4 示例4：格式化输出
    // --------------------------------------------

    println!("\n2.4 示例4：格式化输出");
    println!("------------------------------------------------------------");

    let x = 10;
    let y = 20;

    // 可以在{}中指定位置
    // {0}表示第一个参数，{1}表示第二个参数
    println!("{0} + {1} = {2}", x, y, x + y);

    // 也可以命名参数
    println!("{a} * {b} = {result}", a = x, b = y, result = x * y);
}

/*
==============================================
🔍 深入理解：为什么Rust需要所有权系统？（选学）
==============================================

如果你想知道"为什么"，可以阅读这部分。
如果觉得难，可以跳过，不影响后续学习。

【深入内容】

为什么这样设计？
- C/C++：手动管理内存，容易出错（忘记释放、重复释放、使用已释放的内存）
- Java/Python：垃圾回收，有性能开销和不可预测的暂停
- Rust：编译时检查，既安全又高效

底层原理是什么？
- 所有权系统在编译时追踪每个值的所有者
- 当所有者离开作用域，值自动释放
- 编译器保证不会有悬垂指针或重复释放

与其他语言的对比：
- C/C++：malloc/free，程序员负责
- Java：垃圾回收器负责
- Rust：编译器负责检查，运行时零开销

实际应用场景分析：
- 系统编程：操作系统、驱动、嵌入式
- 高性能服务：Web服务器、数据库
- 命令行工具：ripgrep、fd等
- WebAssembly：在浏览器中运行

==============================================
*/


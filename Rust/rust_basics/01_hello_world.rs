fn part_four_common_errors() {
    println!("============================================================");
    println!("常见错误和陷阱 ⭐⭐⭐⭐⭐");
    println!("============================================================");

    // --------------------------------------------
    // 4.1 错误1：忘记感叹号
    // --------------------------------------------

    println!("4.1 错误1：忘记感叹号");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    println("Hello");  // 缺少感叹号
    ```

    编译器错误信息：
    error[E0423]: expected function, found macro `println`
    help: use `!` to invoke the macro: `println!`

    原因：
    - println是宏，不是函数
    - 宏调用必须使用感叹号！

    ✅ 正确代码：
    ```rust
    println!("Hello");  // 有感叹号
    ```

    记忆技巧：
    - 看到println就想到感叹号
    - 宏 = 感叹号（macro = !）
    */

    // --------------------------------------------
    // 4.2 错误2：占位符数量不匹配
    // --------------------------------------------

    println!("\n4.2 错误2：占位符数量不匹配");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    println!("Hello, {}!");  // 有占位符但没有参数
    ```

    编译器错误信息：
    error: 1 positional argument in format string, but no arguments were given

    原因：
    - 格式字符串中有{}占位符
    - 但没有提供对应的参数

    ✅ 正确代码：
    ```rust
    println!("Hello, {}!", "Rust");  // 提供参数
    // 或者
    println!("Hello!");  // 不使用占位符
    ```

    记忆技巧：
    - 有几个{}就要有几个参数
    - 编译器会明确告诉你缺少参数
    */

    // --------------------------------------------
    // 4.3 错误3：main函数拼写错误
    // --------------------------------------------

    println!("\n4.3 错误3：main函数拼写错误");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    fn Main() {  // 大写M
        println!("Hello");
    }
    ```

    编译器错误信息：
    error[E0601]: `main` function not found in crate

    原因：
    - Rust程序入口必须是main函数（小写）
    - Main（大写）不是入口函数

    ✅ 正确代码：
    ```rust
    fn main() {  // 小写m
        println!("Hello");
    }
    ```

    记忆技巧：
    - main必须小写
    - Rust区分大小写
    */

    // --------------------------------------------
    // 4.4 错误4：忘记分号
    // --------------------------------------------

    println!("\n4.4 错误4：忘记分号");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    fn main() {
        println!("Hello")  // 缺少分号
    }
    ```

    编译器错误信息：
    error: expected `;`, found `}`

    原因：
    - Rust语句需要分号结尾
    - 宏调用也是语句，需要分号

    ✅ 正确代码：
    ```rust
    fn main() {
        println!("Hello");  // 有分号
    }
    ```

    记忆技巧：
    - 语句结尾加分号
    - 表达式不需要分号（后面会学）
    */

    // --------------------------------------------
    // 4.5 错误5：字符串引号不匹配
    // --------------------------------------------

    println!("\n4.5 错误5：字符串引号不匹配");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    println!("Hello');  // 开始是"，结束是'
    ```

    编译器错误信息：
    error: unterminated double quote string

    原因：
    - 字符串必须用双引号"包围
    - 单引号'用于字符（单个字符）

    ✅ 正确代码：
    ```rust
    println!("Hello");  // 双引号
    let c = 'A';  // 单引号用于字符
    ```

    记忆技巧：
    - 字符串 = 双引号"
    - 字符 = 单引号'
    */

    // --------------------------------------------
    // 4.6 错误6：中文字符导致的问题
    // --------------------------------------------

    println!("\n4.6 错误6：中文字符导致的问题");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    println!（"Hello"）；  // 使用了中文括号和分号
    ```

    编译器错误信息：
    error: unknown start of token: \u{ff08}

    原因：
    - 使用了中文标点符号（、；等）
    - Rust只识别英文标点

    ✅ 正确代码：
    ```rust
    println!("Hello");  // 英文括号和分号
    ```

    记忆技巧：
    - 代码中只用英文标点
    - 字符串内容可以是中文
    - 注释可以是中文
    */

    // --------------------------------------------
    // 4.7 错误7：文件编码问题
    // --------------------------------------------

    println!("\n4.7 错误7：文件编码问题");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误情况：
    - 文件保存为GBK或其他编码
    - 中文注释或字符串显示乱码

    原因：
    - Rust源文件必须是UTF-8编码
    - 其他编码会导致编译错误或乱码

    ✅ 正确做法：
    - VS Code默认使用UTF-8
    - 确保文件编码设置为UTF-8
    - 右下角可以看到和修改编码

    记忆技巧：
    - Rust = UTF-8
    - 现代编程语言都用UTF-8
    */
}


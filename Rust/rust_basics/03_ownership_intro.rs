/*
【核心概念】所有权系统

是什么？
- Rust最独特和核心的特性
- 一套管理内存的规则，在编译时检查
- 保证内存安全，无需垃圾回收

为什么需要？
- C/C++：手动管理内存，容易出错
- Java/Python：垃圾回收，有性能开销
- Rust：编译时检查，既安全又高效

核心特点？
- 每个值都有一个所有者
- 同一时间只能有一个所有者
- 所有者离开作用域，值被释放

Rust特色？
- 零运行时开销的内存安全
- 编译时防止内存泄漏和悬垂指针
- 无需垃圾回收器
*/


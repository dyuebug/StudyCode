fn part_four_common_errors() {
    println!("============================================================");
    println!("常见错误和陷阱 ⭐⭐⭐⭐⭐");
    println!("============================================================");

    // --------------------------------------------
    // 4.1 错误1：使用已移动的值
    // --------------------------------------------

    println!("4.1 错误1：使用已移动的值");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let s1 = String::from("hello");
    let s2 = s1;
    println!("{}", s1);  // 错误！
    ```

    编译器错误信息：
    error[E0382]: borrow of moved value: `s1`
    note: move occurs because `s1` has type `String`, which does not implement the `Copy` trait

    原因：
    - s1的所有权已移动到s2
    - s1不能再使用

    ✅ 正确代码：
    ```rust
    let s1 = String::from("hello");
    let s2 = s1.clone();  // 克隆
    println!("{}, {}", s1, s2);  // 都可以使用
    ```

    记忆技巧：
    - 移动后原变量失效
    - 需要两个副本就用clone()
    */

    // --------------------------------------------
    // 4.2 错误2：函数调用后使用
    // --------------------------------------------

    println!("\n4.2 错误2：函数调用后使用");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let s = String::from("hello");
    takes_ownership(s);
    println!("{}", s);  // 错误！
    ```

    编译器错误信息：
    error[E0382]: borrow of moved value: `s`

    原因：
    - 调用函数时，所有权移动到函数
    - 函数结束后，值被drop
    - s不能再使用

    ✅ 正确代码：
    ```rust
    let s = String::from("hello");
    let s = takes_and_gives_back(s);  // 接收返回值
    println!("{}", s);  // 现在可以使用
    ```

    记忆技巧：
    - 函数参数会获取所有权
    - 需要继续使用就让函数返回
    */

    // --------------------------------------------
    // 4.3 错误3：混淆移动和复制
    // --------------------------------------------

    println!("\n4.3 错误3：混淆移动和复制");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误理解：
    "为什么整数可以用，String不行？"

    ```rust
    let x = 5;
    let y = x;
    println!("{}", x);  // OK

    let s1 = String::from("hello");
    let s2 = s1;
    println!("{}", s1);  // 错误！
    ```

    原因：
    - 整数实现了Copy trait，会复制
    - String没有实现Copy，会移动

    哪些类型会复制？
    - 所有整数类型（i32, u64等）
    - 布尔类型（bool）
    - 浮点类型（f32, f64）
    - 字符类型（char）
    - 元组（如果所有元素都是Copy）

    哪些类型会移动？
    - String
    - Vec
    - 其他堆分配的类型

    记忆技巧：
    - 栈数据 = 复制
    - 堆数据 = 移动
    */

    // --------------------------------------------
    // 4.4 错误4：忘记clone的开销
    // --------------------------------------------

    println!("\n4.4 错误4：忘记clone的开销");
    println!("------------------------------------------------------------");

    /*
    ⚠️ 性能陷阱：
    ```rust
    let s1 = String::from("very long string...");
    let s2 = s1.clone();  // 深拷贝，可能很慢
    ```

    原因：
    - clone()会复制所有数据
    - 对于大数据，开销很大

    ✅ 更好的做法：
    - 使用引用（后面会学）
    - 只在真正需要副本时才clone

    记忆技巧：
    - clone = 深拷贝 = 可能慢
    - 能不clone就不clone
    */

    // --------------------------------------------
    // 4.5 错误5：作用域理解错误
    // --------------------------------------------

    println!("\n4.5 错误5：作用域理解错误");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let s;
    {
        let temp = String::from("hello");
        s = temp;  // temp移动到s
    }  // temp的作用域结束
    println!("{}", s);  // 错误！s指向的数据已被释放
    ```

    编译器错误信息：
    error[E0597]: `temp` does not live long enough

    原因：
    - temp在内层作用域创建
    - 移动到s后，temp的作用域结束
    - 但数据的所有者仍是temp，数据被释放
    - s变成悬垂指针

    ✅ 正确代码：
    ```rust
    let s;
    {
        s = String::from("hello");  // 直接赋值给s
    }
    println!("{}", s);  // OK
    ```

    记忆技巧：
    - 数据的生命周期由所有者决定
    - 所有者离开作用域，数据被释放
    */
}


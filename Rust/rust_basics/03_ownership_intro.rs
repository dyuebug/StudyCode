// 第一部分：核心概念详解 ⭐⭐⭐⭐⭐
// ============================================

fn main() {
    println!("第一部分：核心概念详解");

    // --------------------------------------------
    // 1.1 概念1：所有权三规则
    // --------------------------------------------

    println!("1.1 所有权三规则");

    /*
    【所有权三规则】

    规则1：每个值都有一个所有者
    - 每个值都被一个变量"拥有"
    - 这个变量就是值的所有者

    规则2：同一时间只能有一个所有者
    - 一个值不能同时被多个变量拥有
    - 所有权可以转移，但不能共享

    规则3：所有者离开作用域，值被释放
    - 当所有者变量离开作用域
    - 它拥有的值会被自动释放（drop）

    为什么需要这些规则？
    - 防止内存泄漏：值一定会被释放
    - 防止重复释放：只有一个所有者
    - 防止使用已释放的内存：所有权转移后不能再用
    */

    // --------------------------------------------
    // 1.2 概念2：作用域（Scope）
    // --------------------------------------------

    println!("1.2 作用域");

    /*
    【作用域】

    是什么？
    - 变量有效的代码范围
    - 通常由{}花括号定义

    为什么需要？
    - 确定变量何时创建和销毁
    - 管理资源的生命周期

    核心特点：
    - 变量在声明时进入作用域
    - 离开作用域时自动销毁
    - 内层作用域可以访问外层变量

    Rust中的实现：
    - 作用域结束时自动调用drop函数
    - 释放变量拥有的资源
    */

    // --------------------------------------------
    // 1.3 概念3：移动（Move）
    // --------------------------------------------

    println!("1.3 移动");

    /*
    【移动】

    是什么？
    - 所有权从一个变量转移到另一个变量
    - 原变量失效，不能再使用

    为什么需要？
    - 保证同一时间只有一个所有者
    - 防止重复释放内存

    核心特点：
    - 赋值时发生移动（对于堆数据）
    - 原变量不能再使用
    - 新变量成为所有者

    Rust特色：
    - 栈数据（如整数）会复制，不会移动
    - 堆数据（如String）会移动
    - 编译器会检查移动后的使用
    */

    // --------------------------------------------
    // 1.4 概念4：克隆（Clone）
    // --------------------------------------------

    println!("1.4 克隆");

    /*
    【克隆】

    是什么？
    - 深拷贝数据，创建新的副本
    - 两个变量各自拥有独立的数据

    为什么需要？
    - 有时需要多个数据副本
    - 避免所有权转移

    核心特点：
    - 显式调用clone()方法
    - 创建完整的数据副本
    - 可能有性能开销

    与移动的区别：
    - 移动：转移所有权，原变量失效
    - 克隆：复制数据，两个变量都有效
    */
}


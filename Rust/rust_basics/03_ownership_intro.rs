fn part_five_concept_reference() {
    println!("============================================================");
    println!("第五部分：概念速查");
    println!("============================================================");

    /*
    【所有权三规则】

    1. 每个值都有一个所有者
    2. 同一时间只能有一个所有者
    3. 所有者离开作用域，值被释放

    ────────────────────────────────────

    【移动 vs 复制 vs 克隆】

    移动（Move）：
    - 所有权转移
    - 原变量失效
    - 堆数据默认行为

    复制（Copy）：
    - 创建副本
    - 两个变量都有效
    - 栈数据默认行为

    克隆（Clone）：
    - 深拷贝
    - 显式调用clone()
    - 可能有性能开销

    ────────────────────────────────────

    【实现Copy的类型】

    - 整数：i8, i16, i32, i64, i128, isize
    - 无符号整数：u8, u16, u32, u64, u128, usize
    - 浮点数：f32, f64
    - 布尔：bool
    - 字符：char
    - 元组：如果所有元素都是Copy

    ────────────────────────────────────

    【不实现Copy的类型】

    - String
    - Vec<T>
    - Box<T>
    - 其他堆分配的类型

    ────────────────────────────────────

    【记忆口诀】

    栈上数据会复制，
    堆上数据会移动。
    需要副本用clone，
    所有权规则要记牢。
    */
}


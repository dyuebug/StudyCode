fn part_four_common_errors() {
    println!("============================================================");
    println!("常见错误和陷阱 ⭐⭐⭐⭐⭐");
    println!("============================================================");

    // --------------------------------------------
    // 4.1 错误1：修改不可变变量
    // --------------------------------------------

    println!("4.1 错误1：修改不可变变量");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let x = 5;
    x = 6;  // 错误！
    ```

    编译器错误信息：
    error[E0384]: cannot assign twice to immutable variable `x`
    help: consider making this binding mutable: `mut x`

    原因：
    - 变量x默认不可变
    - 不能给不可变变量重新赋值

    ✅ 正确代码：
    ```rust
    let mut x = 5;  // 添加mut关键字
    x = 6;  // 现在可以修改了
    ```

    记忆技巧：
    - 需要修改就加mut
    - 编译器会提示你加mut
    */

    // --------------------------------------------
    // 4.2 错误2：未使用的mut
    // --------------------------------------------

    println!("\n4.2 错误2：未使用的mut");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let mut x = 5;  // 声明为可变
    println!("{}", x);  // 但从未修改
    ```

    编译器警告信息：
    warning: variable does not need to be mutable
    help: remove this `mut`

    原因：
    - 声明为可变但从未修改
    - 不必要的mut会误导读者

    ✅ 正确代码：
    ```rust
    let x = 5;  // 去掉mut
    println!("{}", x);
    ```

    记忆技巧：
    - 只在真正需要修改时才用mut
    - 听从编译器的建议
    */

    // --------------------------------------------
    // 4.3 错误3：使用未初始化的变量
    // --------------------------------------------

    println!("\n4.3 错误3：使用未初始化的变量");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let x: i32;
    println!("{}", x);  // 错误：x未初始化
    ```

    编译器错误信息：
    error[E0381]: borrow of possibly-uninitialized variable: `x`

    原因：
    - 变量声明后必须初始化才能使用
    - Rust不允许使用未初始化的变量

    ✅ 正确代码：
    ```rust
    let x: i32 = 0;  // 初始化
    println!("{}", x);
    ```

    记忆技巧：
    - 声明时就初始化
    - 或者在使用前确保已初始化
    */

    // --------------------------------------------
    // 4.4 错误4：类型不匹配
    // --------------------------------------------

    println!("\n4.4 错误4：类型不匹配");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let x: i32 = "hello";  // 错误：类型不匹配
    ```

    编译器错误信息：
    error[E0308]: mismatched types
    expected `i32`, found `&str`

    原因：
    - 声明为i32类型
    - 但赋值为字符串

    ✅ 正确代码：
    ```rust
    let x: i32 = 42;  // 整数
    let y: &str = "hello";  // 字符串
    ```

    记忆技巧：
    - 类型必须匹配
    - 编译器会明确告诉你期望什么类型
    */

    // --------------------------------------------
    // 4.5 错误5：整数溢出
    // --------------------------------------------

    println!("\n4.5 错误5：整数溢出");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let x: i8 = 127;
    let y = x + 1;  // 溢出！i8最大值是127
    ```

    编译器行为：
    - Debug模式：panic（程序崩溃）
    - Release模式：环绕（wrapping，变成-128）

    原因：
    - i8范围是-128到127
    - 127 + 1超出范围

    ✅ 正确代码：
    ```rust
    let x: i8 = 127;
    let y = x.checked_add(1);  // 返回Option，安全检查
    match y {
        Some(val) => println!("结果：{}", val),
        None => println!("溢出了！"),
    }
    ```

    记忆技巧：
    - 注意数据范围
    - 使用checked_*方法安全计算
    */

    // --------------------------------------------
    // 4.6 错误6：shadowing vs mut混淆
    // --------------------------------------------

    println!("\n4.6 错误6：shadowing vs mut混淆");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误理解：
    ```rust
    let mut x = 5;
    x = "hello";  // 错误：不能改变类型
    ```

    编译器错误信息：
    error[E0308]: mismatched types

    原因：
    - mut只能修改值，不能改变类型
    - 需要用shadowing才能改变类型

    ✅ 正确代码：
    ```rust
    let x = 5;
    let x = "hello";  // shadowing，可以改变类型
    ```

    记忆技巧：
    - mut = 修改值，类型不变
    - shadowing = 新变量，可以改变类型
    */

    // --------------------------------------------
    // 4.7 错误7：字符和字符串混淆
    // --------------------------------------------

    println!("\n4.7 错误7：字符和字符串混淆");
    println!("------------------------------------------------------------");

    /*
    ❌ 错误代码：
    ```rust
    let c: char = "A";  // 错误：双引号是字符串
    ```

    编译器错误信息：
    error[E0308]: mismatched types
    expected `char`, found `&str`

    原因：
    - char用单引号'，表示单个字符
    - &str用双引号"，表示字符串

    ✅ 正确代码：
    ```rust
    let c: char = 'A';  // 字符，单引号
    let s: &str = "A";  // 字符串，双引号
    ```

    记忆技巧：
    - 字符 = 单引号 = char
    - 字符串 = 双引号 = &str
    */
}


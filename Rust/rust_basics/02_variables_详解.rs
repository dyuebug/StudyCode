fn part_five_type_reference() {
    println!("============================================================");
    println!("第五部分：类型速查");
    println!("============================================================");

    /*
    【整数类型】

    有符号整数（可以是负数）：
    - i8: -128 到 127
    - i16: -32,768 到 32,767
    - i32: -2,147,483,648 到 2,147,483,647（默认）
    - i64: -9,223,372,036,854,775,808 到 9,223,372,036,854,775,807
    - i128: 更大范围
    - isize: 取决于系统架构（32位或64位）

    无符号整数（只能是非负数）：
    - u8: 0 到 255
    - u16: 0 到 65,535
    - u32: 0 到 4,294,967,295
    - u64: 0 到 18,446,744,073,709,551,615
    - u128: 更大范围
    - usize: 取决于系统架构

    ────────────────────────────────────

    【浮点数类型】

    - f32: 32位浮点数（单精度）
    - f64: 64位浮点数（双精度，默认）

    示例：
    ```rust
    let x = 2.0;  // f64
    let y: f32 = 3.0;  // f32
    ```

    ────────────────────────────────────

    【布尔类型】

    - bool: true 或 false

    示例：
    ```rust
    let t = true;
    let f: bool = false;
    ```

    ────────────────────────────────────

    【字符类型】

    - char: Unicode字符，4字节

    示例：
    ```rust
    let c = 'A';
    let emoji = '😀';
    let chinese = '中';
    ```

    注意：
    - 用单引号
    - 支持所有Unicode字符
    - 占4字节（不是1字节）
    */
}


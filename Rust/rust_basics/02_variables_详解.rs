/*
【核心概念】变量和数据类型

是什么？
- 变量：存储数据的容器，有名字和类型
- 数据类型：定义变量可以存储什么样的数据

为什么需要？
- 程序需要存储和操作数据
- 类型系统帮助编译器检查错误
- Rust的类型系统保证内存安全

核心特点？
- 默认不可变：变量默认不能修改
- 类型推断：编译器可以自动推断类型
- 强类型：类型不能隐式转换

Rust特色？
- 变量默认不可变（immutable）
- 需要mut关键字才能修改
- 变量遮蔽（shadowing）允许重新声明
*/

// ============================================
// 第一部分：核心概念详解 ⭐⭐⭐⭐⭐
// ============================================

fn main() {
    println!("第一部分：核心概念详解");

    // --------------------------------------------
    // 1.1 概念1：变量声明
    // --------------------------------------------

    println!("1.1 变量声明");

    /*
    【变量声明】

    是什么？
    - 使用let关键字声明变量
    - 格式：let 变量名 = 值;

    为什么需要？
    - 存储数据供后续使用
    - 给数据起一个有意义的名字

    核心特点：
    - 默认不可变（immutable）
    - 必须初始化才能使用
    - 可以类型推断

    Rust中的实现：
    - let x = 5;  // 不可变变量
    - let mut y = 10;  // 可变变量
    */

    // --------------------------------------------
    // 1.2 概念2：可变性（Mutability）
    // --------------------------------------------

    println!("1.2 可变性");

    /*
    【可变性】

    是什么？
    - 变量是否可以被修改的属性
    - 默认不可变，需要mut关键字才可变

    为什么需要？
    - 不可变性提高代码安全性
    - 编译器可以做更多优化
    - 明确标记哪些数据会改变

    核心特点：
    - 默认不可变（Rust哲学）
    - mut关键字显式声明可变
    - 编译器会检查是否真的需要mut

    Rust特色：
    - 与大多数语言相反（其他语言默认可变）
    - 鼓励使用不可变数据
    - 提高并发安全性
    */

    // --------------------------------------------
    // 1.3 概念3：变量遮蔽（Shadowing）
    // --------------------------------------------

    println!("1.3 变量遮蔽");

    /*
    【变量遮蔽】

    是什么？
    - 用相同名字声明新变量，遮蔽旧变量
    - 使用let关键字重新声明

    为什么需要？
    - 可以改变变量的类型
    - 可以改变变量的可变性
    - 避免起新名字（如x1, x2, x3）

    核心特点：
    - 创建新变量，不是修改旧变量
    - 可以改变类型
    - 旧变量仍然存在，只是被遮蔽

    与mut的区别：
    - mut：修改同一个变量，类型不变
    - shadowing：创建新变量，可以改变类型
    */
}


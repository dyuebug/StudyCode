fn part_two_basic_practice() {
    println!("============================================================");
    println!("第二部分：基础操作实践");
    println!("============================================================");

    // --------------------------------------------
    // 2.1 示例1：不可变变量
    // --------------------------------------------

    println!("2.1 示例1：不可变变量");
    println!("------------------------------------------------------------");

    // 声明不可变变量
    // let关键字声明变量
    // x是变量名
    // 5是值（整数字面量）
    let x = 5;

    // 打印变量的值
    println!("x的值是：{}", x);

    // 下面这行会报错（取消注释试试）
    // x = 6;  // 错误：不能给不可变变量赋值

    // --------------------------------------------
    // 2.2 示例2：可变变量
    // --------------------------------------------

    println!("\n2.2 示例2：可变变量");
    println!("------------------------------------------------------------");

    // 声明可变变量
    // mut关键字表示可变
    let mut y = 10;

    println!("y的初始值：{}", y);

    // 修改可变变量的值
    // 因为有mut，所以可以修改
    y = 15;

    println!("y的新值：{}", y);

    // --------------------------------------------
    // 2.3 示例3：类型标注
    // --------------------------------------------

    println!("\n2.3 示例3：类型标注");
    println!("------------------------------------------------------------");

    // 显式指定类型
    // :i32表示32位有符号整数
    let a: i32 = 100;

    // 编译器可以推断类型
    // 这里推断为i32（默认整数类型）
    let b = 200;

    println!("a = {}, b = {}", a, b);

    // --------------------------------------------
    // 2.4 示例4：变量遮蔽
    // --------------------------------------------

    println!("\n2.4 示例4：变量遮蔽");
    println!("------------------------------------------------------------");

    // 第一次声明
    let z = 5;
    println!("第一次：z = {}", z);

    // 遮蔽：用相同名字声明新变量
    // 这是新变量，不是修改旧变量
    let z = z + 1;
    println!("第二次：z = {}", z);

    // 再次遮蔽，甚至可以改变类型
    let z = "现在是字符串了";
    println!("第三次：z = {}", z);

    // --------------------------------------------
    // 2.5 示例5：基本数据类型
    // --------------------------------------------

    println!("\n2.5 示例5：基本数据类型");
    println!("------------------------------------------------------------");

    // 整数类型
    let int8: i8 = 127;  // 8位有符号整数（-128到127）
    let int32: i32 = 2147483647;  // 32位有符号整数
    let uint: u32 = 4294967295;  // 32位无符号整数（0到4294967295）

    println!("i8: , i32: {}, u32: {}", int8, int32, uint);

    // 浮点数类型
    let float32: f32 = 3.14;  // 32位浮点数
    let float64: f64 = 2.718281828;  // 64位浮点数（默认）

    println!("f32: {}, f64: {}", float32, float64);

    // 布尔类型
    let is_rust_awesome: bool = true;  // 布尔值：true或false
    let is_learning: bool = false;

    println!("Rust awesome? , Learning? ", is_rust_awesome, is_learning);

    // 字符类型
    let letter: char = 'A';  // 单个Unicode字符，用单引号
    let emoji: char = '😀';  // 支持emoji

    println!("字符: {}, Emoji: {}", letter, emoji);

    // --------------------------------------------
    // 2.6 示例6：类型转换
    // --------------------------------------------

    println!("\n2.6 示例6：类型转换");
    println!("------------------------------------------------------------");

    // Rust不允许隐式类型转换
    let num_i32: i32 = 100;

    // 显式转换：使用as关键字
    let num_i64: i64 = num_i32 as i64;
    let num_f64: f64 = num_i32 as f64;

    println!("i32: {}, i64: {}, f64: {}", num_i32, num_i64, num_f64);
}

/*
==============================================
🔍 深入理解：为什么Rust变量默认不可变？（选学）
==============================================

如果你想知道"为什么"，可以阅读这部分。
如果觉得难，可以跳过，不影响后续学习。

【深入内容】

为什么这样设计？
- 不可变性使代码更容易理解和维护
- 减少bug：不会意外修改不该修改的数据
- 并发安全：多个线程可以安全地读取不可变数据
- 编译器优化：不可变数据可以做更多优化

底层原理是什么？
- 不可变变量存储在栈上，编译器知道它不会改变
- 可变变量需要额外的检查和管理
- 所有权系统配合不可变性，保证内存安全

与其他语言的对比：
- JavaScript/Python：变量默认可变
- Java：需要final关键字才不可变
- Rust：默认不可变，需要mut才可变

实际应用场景分析：
- 配置数据：通常不需要修改，用不可变
- 计数器：需要修改，用mut
- 函数式编程：倾向于使用不可变数据

Rust编译器如何处理：
- 编译时检查：不可变变量不能被赋值
- 借用检查器：确保可变引用的唯一性
- 优化：不可变数据可以放在只读内存区

==============================================
*/


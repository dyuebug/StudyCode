/*
【概念1：#include 预处理指令】

是什么？
- 告诉编译器："我要用某个工具包里的功能"
- <iostream> 是"输入输出流"工具包，提供 cout（输出）和 cin（输入）

类比：
- 就像做菜前要先准备食材
- #include <iostream> = "请把输入输出的工具准备好"

语法：
- #include <标准库头文件>    → 引入C++标准库
- #include "自定义头文件"    → 引入自己写的文件（后续会学到）

注意：
- #include 前面有 # 号，这是"预处理指令"的标志
- 不需要分号结尾（它不是普通语句）
*/

/*
【概念2：using namespace std】

是什么？
- 告诉编译器："我要用标准库（std）里的东西，不用每次都写全名"

类比：
- 就像你在公司里叫同事"小王"而不是"技术部-开发组-王某某"
- 没有这行，你就得写 std::cout 而不是 cout

注意：
- 在大型项目中（比如MAA），通常不会用 using namespace std
- 因为可能和其他库的名字冲突
- 但学习阶段用它完全没问题，简化代码
*/

/*
【概念3：main() 函数】

是什么？
- 程序的入口点，C++程序从这里开始执行
- 每个C++程序有且只有一个 main 函数

语法：
  int main() {
      // 你的代码写在这里
      return 0;
  }

参数说明：
- int → 返回值类型，表示返回一个整数
- main → 函数名，固定写法
- () → 参数列表，暂时为空
- {} → 函数体，代码写在大括号里
- return 0 → 返回0表示"程序正常结束"

类比：
- main() 就像一本书的"正文"
- #include 是"前言"（准备工作）
- return 0 是"结语"（告诉系统一切正常）
*/

/*
【概念4：cout 输出】

是什么？
- cout（读作 see-out）是"字符输出"的意思
- 用来在屏幕上显示文字

语法：
  cout << "要显示的内容";

符号说明：
- << 叫"插入运算符"，把右边的内容"送入"cout
- 可以理解为：cout ← "内容"（内容流向屏幕）
- endl 表示换行（end line 的缩写）

类比：
- cout 就像一个喇叭，<< 就是"对着喇叭说"
- cout << "你好"; → 对着喇叭说"你好"
*/


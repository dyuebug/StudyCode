/*
【核心概念】C++程序的基本结构

是什么？
- 每个C++程序都有固定的"骨架"：头文件引入 → main函数 → 执行语句 → 返回值

为什么需要？
- 这是C++编译器能识别的最小完整程序
- 就像写信有固定格式（称呼→正文→落款），C++程序也有固定结构

核心特点：
- #include 引入功能（类似"导入工具包"）
- main() 是程序入口（所有代码从这里开始执行）
- 每条语句以分号 ; 结尾
*/


/*
==============================================
🔍 深入理解：引用的底层实现（选学）
==============================================

如果你想知道"为什么"，可以阅读这部分。
如果觉得难，可以跳过，不影响后续学习。

【引用的汇编实现】

C++ 代码：
int number = 100;
int & ref = number;
ref = 200;

汇编代码（简化）：
mov dword ptr [rbp-4], 100    ; number = 100
lea rax, [rbp-4]              ; rax = &number
mov qword ptr [rbp-12], rax   ; ref = &number（存储地址）
mov rax, qword ptr [rbp-12]   ; rax = ref（加载地址）
mov dword ptr [rax], 200      ; *rax = 200（间接寻址）

可以看出：
- 引用本质上存储的是地址
- 对引用的操作是间接寻址
- 引用 ≈ 常量指针

【引用的内存布局】

int number = 100;
int & ref = number;

内存布局：
+--------+--------+
| number |  ref   |
|  100   | &number|
+--------+--------+
  4字节    8字节（64位系统）

注意：
- ref 存储的是 number 的地址
- 但编译器阻止直接访问 ref 本身
- 对 ref 的操作总是间接访问 number

【引用与指针的性能对比】

引用：
- 编译器优化后，性能与直接访问相同
- 不需要解引用操作
- 更容易被编译器优化

指针：
- 需要解引用操作
- 可能影响编译器优化
- 灵活但开销稍大

结论：
- 引用的性能 ≥ 指针的性能
- 推荐使用引用代替指针

【为什么引用必须初始化？】

引用的本质是常量指针：
int & ref = number;
// 等价于
int * const ref = &number;

常量指针必须初始化：
int * const ptr;  // 错误：常量指针必须初始化
int * const ptr = &number;  // 正确

因此引用也必须初始化。

【为什么引用不能更改绑定？】

引用的本质是常量指针：
int & ref = number1;
ref = number2;  // 这不是更改绑定

实际执行的是：
*ref = number2;  // 将 number2 的值赋给 number1

因为 ref 是常量指针，不能更改指向。

==============================================
*/


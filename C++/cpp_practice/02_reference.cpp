// 1.4 引用作为函数参数 ⭐⭐⭐⭐⭐（重点）
// --------------------------------------------

/*
【引用作为函数参数】

为什么需要？
- 值传递：会复制实参，开销大
- 指针传递：不复制，但使用不便
- 引用传递：不复制，使用方便

优点：
1. 避免复制，提高效率
2. 可以修改实参的值
3. 使用方便，像普通变量一样

适用场景：
1. 需要修改实参的值
2. 参数是大对象，避免复制开销
3. 需要返回多个值
*/

// 值传递：会复制实参
void swap_by_value(int x, int y) {
    int temp = x;
    x = y;
    y = temp;
    // x 和 y 是实参的副本，修改不影响实参
}

// 指针传递：不复制，但使用不便
void swap_by_pointer(int * px, int * py) {
    int temp = *px;
    *px = *py;
    *py = temp;
    // 需要解引用，容易出错
}

// 引用传递：不复制，使用方便 ⭐⭐⭐⭐⭐
void swap_by_reference(int & x, int & y) {
    int temp = x;
    x = y;
    y = temp;
    // x 和 y 是实参的引用，修改会影响实参
}

void test_reference_as_parameter() {
    cout << "\n=== 引用作为函数参数 ===" << endl;

    int a = 10, b = 20;

    // 值传递：不会交换
    cout << "值传递前：a = " << a << ", b = " << b << endl;
    swap_by_value(a, b);
    cout << "值传递后：a = " << a << ", b = " << b << endl;  // 不变

    // 指针传递：会交换
    cout << "\n指针传递前：a = " << a << ", b = " << b << endl;
    swap_by_pointer(&a, &b);
    cout << "指针传递后：a = " << a << ", b = " << b << endl;  // 交换

    // 引用传递：会交换
    cout << "\n引用传递前：a = " << a << ", b = " << b << endl;
    swap_by_reference(a, b);
    cout << "引用传递后：a = " << a << ", b = " << b << endl;  // 交换
}


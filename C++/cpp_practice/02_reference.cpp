// 1.6 引用作为函数返回值 ⭐⭐⭐⭐
// --------------------------------------------

/*
【引用作为函数返回值】

为什么需要？
- 避免返回时的复制开销
- 实现链式调用

注意事项：
1. 不要返回局部变量的引用（局部变量会被销毁）
2. 不要轻易返回堆空间变量的引用（容易内存泄漏）
3. 可以返回全局变量、静态变量、参数的引用

返回引用 vs 返回值：
- 返回值：会复制，开销大
- 返回引用：不复制，开销小
*/

// 全局变量
int g_number = 100;

// 返回全局变量的引用（安全）
int & get_global_number() {
    return g_number;
}

// 返回局部变量的引用（危险，不要这样做）
int & get_local_number() {
    int local = 100;
    return local;  // 危险：local 会被销毁
}

// 返回参数的引用（安全）
int & get_max(int & a, int & b) {
    return a > b ? a : b;
}

void test_reference_as_return() {
    cout << "\n=== 引用作为函数返回值 ===" << endl;

    // 返回全局变量的引用
    int & ref1 = get_global_number();
    cout << "ref1 = " << ref1 << endl;  // 100
    ref1 = 200;  // 修改全局变量
    cout << "g_number = " << g_number << endl;  // 200

    // 返回参数的引用
    int x = 10, y = 20;
    int & ref2 = get_max(x, y);
    cout << "max = " << ref2 << endl;  // 20
    ref2 = 30;  // 修改 y
    cout << "y = " << y << endl;  // 30

    // 链式调用
    get_max(x, y) = 40;  // 直接修改较大的值
    cout << "y = " << y << endl;  // 40
}


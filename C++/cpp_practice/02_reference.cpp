/*
【核心概念】引用（Reference）

是什么？
- 在逻辑层面上，引用是一个已定义变量的别名
- 在底层实现上，引用是一种受限制的指针

为什么需要？
- 避免函数参数传递时的复制开销
- 提供比指针更安全、更易用的间接访问方式
- 实现函数返回多个值

核心特点：
- 必须在声明时初始化
- 一经绑定，无法更改绑定
- 引用本身不占用额外空间（逻辑上）
- 对引用的操作就是对原变量的操作
*/


/*
【常引用】

是什么？
- 使用 const 修饰的引用
- 语法：const 类型 & 引用名 = 变量名;

为什么需要？
- 避免通过引用修改原变量
- 避免复制大对象
- 提高代码安全性

适用场景：
- 函数参数：不需要修改实参，但要避免复制
- 绑定临时对象：普通引用不能绑定临时对象，常引用可以

特点：
- 不能通过常引用修改原变量
- 可以绑定到常量、变量、临时对象
*/

// 常引用作为函数参数
void print_number(const int & num) {
    cout << "num = " << num << endl;
    // num = 100;  // 错误：不能通过常引用修改值
}

// 普通引用 vs 常引用
void test_const_reference() {
    cout << "\n=== 常引用 ===" << endl;

    int number = 100;

    // 普通引用
    int & ref1 = number;
    ref1 = 200;  // 可以修改
    cout << "number = " << number << endl;  // 200

    // 常引用
    const int & ref2 = number;
    // ref2 = 300;  // 错误：不能通过常引用修改
    cout << "ref2 = " << ref2 << endl;  // 200

    // 常引用可以绑定到临时对象
    const int & ref3 = 100;  // OK
    // int & ref4 = 100;     // 错误：普通引用不能绑定临时对象

    cout << "ref3 = " << ref3 << endl;  // 100

    // 常引用作为函数参数
    print_number(number);
    print_number(100);  // 可以传递临时对象
}


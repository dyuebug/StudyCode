// 1.1 引用的基本概念
// --------------------------------------------

/*
【引用的定义】

语法：类型 & 引用名 = 变量名;

示例：
int number = 10;
int & ref = number;  // ref 是 number 的引用（别名）

注意事项：
1. & 在这里不是取地址符，而是引用符号
2. 引用的类型必须与绑定的变量类型相同
3. 引用必须在声明时初始化
4. 引用一经绑定，无法更改绑定
*/

void test_basic_reference() {
    cout << "\n=== 引用的基本概念 ===" << endl;

    // 定义一个整型变量
    int number = 100;

    // 定义 number 的引用（别名）
    // ref 就是 number 的另一个名字
    int & ref = number;

    // 输出变量和引用的值
    cout << "number = " << number << endl;  // 输出：100
    cout << "ref = " << ref << endl;        // 输出：100

    // 输出变量和引用的地址
    // 引用和原变量的地址相同
    cout << "&number = " << &number << endl;
    cout << "&ref = " << &ref << endl;

    // 通过引用修改值
    // 实际上修改的是 number 的值
    ref = 200;

    cout << "\n修改 ref 后：" << endl;
    cout << "number = " << number << endl;  // 输出：200
    cout << "ref = " << ref << endl;        // 输出：200
}

// --------------------------------------------
// 1.2 引用的本质 ⭐⭐⭐⭐⭐
// --------------------------------------------

/*
【引用的本质】

逻辑层面：
- 引用是变量的别名
- 对引用的操作就是对原变量的操作

底层实现：
- 引用本质上是一种受限制的指针（const pointer）
- 引用占据内存空间（一个指针的大小）
- 编译器阻止对引用本身的访问
- 引用总是指向初始的目标变量

类比：
- 引用 ≈ 常量指针（int * const p）
- 引用必须初始化 ≈ 常量指针必须初始化
- 引用不能更改绑定 ≈ 常量指针不能更改指向
*/

void test_reference_nature() {
    cout << "\n=== 引用的本质 ===" << endl;

    int number = 100;
    int & ref = number;  // 引用

    // 引用占据内存空间
    cout << "sizeof(number) = " << sizeof(number) << endl;  // 4 字节
    cout << "sizeof(ref) = " << sizeof(ref) << endl;        // 4 字节（变量的大小）
    cout << "sizeof(&ref) = " << sizeof(&ref) << endl;      // 8 字节（指针的大小，64位系统）

    // 对引用取地址，得到的是原变量的地址
    cout << "&number = " << &number << endl;
    cout << "&ref = " << &ref << endl;  // 与 &number 相同

    // 引用的底层实现类似于常量指针
    int * const ptr = &number;  // 常量指针
    cout << "*ptr = " << *ptr << endl;  // 输出：100

    // 修改引用 ≈ 修改指针指向的值
    ref = 200;
    cout << "number = " << number << endl;  // 输出：200
    cout << "*ptr = " << *ptr << endl;      // 输出：200
}

// --------------------------------------------
// 1.3 引用与指针的区别 ⭐⭐⭐⭐⭐（面试重点）
// --------------------------------------------

/*
【引用与指针的联系】

1. 都有地址的概念，都用于间接访问变量
2. 引用的底层是指针实现的
3. 引用可以视为受限制的指针（const pointer）

【引用与指针的区别】

1. 初始化要求：
   - 引用：必须在声明时初始化
   - 指针：可以不初始化（但不推荐）

2. 更改绑定/指向：
   - 引用：一经绑定，无法更改
   - 指针：可以随时更改指向

3. 取地址操作：
   - 引用：对引用取地址得到原变量的地址
   - 指针：对指针取地址得到指针本身的地址

4. 空值：
   - 引用：不能为空，必须绑定到有效变量
   - 指针：可以为空（nullptr）

5. 使用方式：
   - 引用：直接使用，像普通变量一样
   - 指针：需要解引用（*ptr）

6. 安全性：
   - 引用：更安全，不会出现空指针、野指针
   - 指针：灵活但容易出错
*/

void test_reference_vs_pointer() {
    cout << "\n=== 引用与指针的区别 ===" << endl;

    int number1 = 100;
    int number2 = 200;

    // 1. 初始化要求
    int & ref = number1;     // 引用必须初始化
    int * ptr;               // 指针可以不初始化（但不推荐）
    ptr = &number1;          // 指针可以后续赋值

    // 2. 更改绑定/指向
    // ref = number2;        // 这不是更改绑定，而是将 number2 的值赋给 number1
    ptr = &number2;          // 指针可以更改指向

    cout << "number1 = " << number1 << endl;  // 100
    cout << "number2 = " << number2 << endl;  // 200
    cout << "ref = " << ref << endl;          // 100（仍然是 number1 的引用）
    cout << "*ptr = " << *ptr << endl;        // 200（指向 number2）

    // 3. 取地址操作
    cout << "&ref = " << &ref << endl;        // number1 的地址
    cout << "&ptr = " << &ptr << endl;        // ptr 本身的地址
    cout << "ptr = " << ptr << endl;          // number2 的地址

    // 4. 使用方式
    ref = 300;               // 直接使用，修改 number1
    *ptr = 400;              // 需要解引用，修改 number2

    cout << "number1 = " << number1 << endl;  // 300
    cout << "number2 = " << number2 << endl;  // 400
}


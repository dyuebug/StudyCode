/*
【常见错误1：引用未初始化】

❌ 错误代码：
int & ref;  // 错误：引用必须初始化

结果：编译错误

原因：引用必须在声明时绑定到一个变量

✅ 正确代码：
int number = 100;
int & ref = number;  // 正确：引用初始化

记忆技巧：
- 引用 = 别名，必须有"本名"
- 引用 = 常量指针，必须初始化


【常见错误2：试图更改引用的绑定】

❌ 错误理解：
int number1 = 100;
int number2 = 200;
int & ref = number1;
ref = number2;  // 误以为更改了绑定

结果：number1 的值变为 200，ref 仍然是 number1 的引用

原因：ref = number2; 是赋值操作，不是更改绑定

✅ 正确理解：
int number1 = 100;
int number2 = 200;
int & ref = number1;
ref = number2;  // 将 number2 的值赋给 number1
cout << number1 << endl;  // 200
cout << number2 << endl;  // 200

记忆技巧：
- 引用一经绑定，无法更改
- 对引用的赋值是对原变量的赋值


【常见错误3：返回局部变量的引用】

❌ 错误代码：
int & func() {
    int local = 100;
    return local;  // 危险：local 会被销毁
}

void test() {
    int & ref = func();
    cout << ref << endl;  // 未定义行为
}

结果：未定义行为，可能输出垃圾值

原因：local 是局部变量，函数返回后被销毁

✅ 正确代码：
int g_number = 100;

int & func() {
    return g_number;  // 安全：全局变量不会被销毁
}

记忆技巧：
- 不要返回局部变量的引用
- 可以返回全局变量、静态变量、参数的引用


【常见错误4：普通引用绑定临时对象】

❌ 错误代码：
int & ref = 100;  // 错误：普通引用不能绑定临时对象

结果：编译错误

原因：临时对象是右值，普通引用只能绑定左值

✅ 正确代码：
const int & ref = 100;  // 正确：常引用可以绑定临时对象

记忆技巧：
- 普通引用：只能绑定左值（变量）
- 常引用：可以绑定左值和右值（临时对象）


【常见错误5：引用作为函数参数时的类型不匹配】

❌ 错误代码：
void func(int & x) {
    x = 100;
}

void test() {
    const int number = 10;
    func(number);  // 错误：const int 不能绑定到 int&
}

结果：编译错误

原因：const int 不能绑定到非 const 引用

✅ 正确代码（方式1）：
void func(const int & x) {  // 使用常引用
    cout << x << endl;
}

void test() {
    const int number = 10;
    func(number);  // 正确
}

✅ 正确代码（方式2）：
void func(int & x) {
    x = 100;
}

void test() {
    int number = 10;  // 去掉 const
    func(number);  // 正确
}

记忆技巧：
- const 变量只能绑定到 const 引用
- 非 const 变量可以绑定到任何引用
*/


// 定义函数：打印问候语，name有默认值
void greet(string name = "朋友") {
    cout << "你好，" << name << "！" << endl;
}

void demonstratePart2_1() {
    cout << string(60, '=') << endl;
    cout << "第二部分：基础操作实践" << endl;
    cout << string(60, '=') << endl;

    cout << "\n2.1 示例1：单个默认参数" << endl;
    cout << string(60, '-') << endl;

    // 调用方式1：不传参数，使用默认值
    cout << "调用 greet()：" << endl;
    greet();  // 输出："你好，朋友！"

    // 调用方式2：传入参数，覆盖默认值
    cout << "调用 greet(\"小明\")：" << endl;
    greet("小明");  // 输出："你好，小明！"
}

// --------------------------------------------
// 2.2 示例2：多个默认参数
// --------------------------------------------

// 定义函数：打印矩形，宽度和高度都有默认值
// 参数1：width - 矩形宽度（默认5）
// 参数2：height - 矩形高度（默认3）
// 参数3：ch - 填充字符（默认'*'）
void printRectangle(int width = 5, int height = 3, char ch = '*') {
    for (int i = 0; i < height; i++) {
        for (int j = 0; j < width; j++) {
            cout << ch;
        }
        cout << endl;
    }
}

void demonstratePart2_2() {
    cout << "\n2.2 示例2：多个默认参数" << endl;
    cout << string(60, '-') << endl;

    // 调用方式1：全部使用默认值
    cout << "调用 printRectangle()：" << endl;
    printRectangle();  // 5x3的*矩形

    cout << endl;

    // 调用方式2：只传第1个参数
    cout << "调用 printRectangle(8)：" << endl;
    printRectangle(8);  // 8x3的*矩形

    cout << endl;

    // 调用方式3：传前2个参数
    cout << "调用 printRectangle(6, 2)：" << endl;
    printRectangle(6, 2);  // 6x2的*矩形

    cout << endl;

    // 调用方式4：传全部参数
    cout << "调用 printRectangle(4, 4, '#')：" << endl;
    printRectangle(4, 4, '#');  // 4x4的#矩形
}

// --------------------------------------------
// 2.3 示例3：默认参数与函数重载配合
// --------------------------------------------

// 使用默认参数，可以减少重载函数的数量

// 方案1：使用函数重载（需要3个函数）
void printInfo1(string name) {
    cout << "姓名：" << name << endl;
}

void printInfo1(string name, int age) {
    cout << "姓名：" << name << "，年龄：" << age << endl;
}

void printInfo1(string name, int age, string city) {
    cout << "姓名：" << name << "，年龄：" << age << "，城市：" << city << endl;
}

// 方案2：使用默认参数（只需1个函数）
void printInfo2(string name, int age = 0, string city = "未知") {
    cout << "姓名：" << name;
    if (age > 0) {
        cout << "，年龄：" << age;
    }
    if (city != "未知") {
        cout << "，城市：" << city;
    }
    cout << endl;
}

void demonstratePart2_3() {
    cout << "\n2.3 示例3：默认参数与函数重载配合" << endl;
    cout << string(60, '-') << endl;

    cout << "方案1：使用函数重载" << endl;
    printInfo1("张三");
    printInfo1("李四", 25);
    printInfo1("王五", 30, "北京");

    cout << "\n方案2：使用默认参数" << endl;
    printInfo2("张三");
    printInfo2("李四", 25);
    printInfo2("王五", 30, "北京");
}


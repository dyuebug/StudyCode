// 1.1 什么是默认参数？
// --------------------------------------------

/*
【默认参数】

是什么？
- 函数参数可以有默认值
- 调用时可以不传这个参数

语法：
返回类型 函数名(类型 参数1, 类型 参数2 = 默认值) {
    // 函数体
}

示例：
void greet(string name = "朋友") {
    cout << "你好，" << name << endl;
}

调用：
greet();           // 使用默认值："你好，朋友"
greet("小明");     // 使用传入值："你好，小明"
*/

// --------------------------------------------
// 1.2 默认参数的规则
// --------------------------------------------

/*
【规则1：从右向左设置】

✅ 正确：
void func(int a, int b = 10, int c = 20);

❌ 错误：
void func(int a = 10, int b, int c = 20);  // b没有默认值，但在中间

原因：
- 调用时参数是从左向右匹配的
- 如果中间有非默认参数，会导致歧义

【规则2：声明和定义分离时，默认值只能出现一次】

✅ 正确：
// 头文件中声明
void func(int a, int b = 10);

// 源文件中定义
void func(int a, int b) {  // 不再写默认值
    // ...
}

❌ 错误：
// 声明
void func(int a, int b = 10);

// 定义
void func(int a, int b = 10) {  // 重复指定默认值，编译错误
    // ...
}

【规则3：默认参数在编译时确定】

void func(int x = 10) {  // 10是编译时常量
    // ...
}

int value = 20;
void func2(int x = value);  // 错误！value不是编译时常量
*/


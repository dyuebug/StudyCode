void demonstratePart4() {
    cout << string(60, '=') << endl;
    cout << "常见错误和陷阱 ⭐⭐⭐⭐⭐" << endl;
    cout << string(60, '=') << endl;

    // --------------------------------------------
    // 4.1 错误1：默认参数不连续
    // --------------------------------------------

    cout << "\n4.1 错误1：默认参数不连续" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
void func(int a = 10, int b, int c = 30);  // 编译错误！

原因：
- b没有默认值，但在有默认值的a和c之间
- 违反了"从右向左连续"的规则

✅ 正确代码：
void func(int a, int b, int c = 30);
void func(int a, int b = 20, int c = 30);

记忆技巧：
- 默认参数必须在最右边
- 一旦开始有默认值，右边的参数都要有默认值
)" << endl;

    // --------------------------------------------
    // 4.2 错误2：重复指定默认值
    // --------------------------------------------

    cout << "\n4.2 错误2：重复指定默认值" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
// 头文件
void func(int a = 10);

// 源文件
void func(int a = 10) {  // 编译错误！重复指定
    // ...
}

原因：
- 默认值只能在声明或定义中出现一次
- 通常在声明（头文件）中指定

✅ 正确代码：
// 头文件
void func(int a = 10);

// 源文件
void func(int a) {  // 不再写默认值
    // ...
}

记忆技巧：
- 默认值写在声明处（头文件）
- 定义处不再重复
)" << endl;

    // --------------------------------------------
    // 4.3 错误3：默认参数使用非常量
    // --------------------------------------------

    cout << "\n4.3 错误3：默认参数使用非常量" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int globalValue = 10;
void func(int a = globalValue);  // 编译错误！

原因：
- 默认参数必须是编译时常量
- globalValue是运行时变量

✅ 正确代码：
const int GLOBAL_VALUE = 10;  // 常量
void func(int a = GLOBAL_VALUE);  // 可以

或者：
void func(int a = 10);  // 直接用字面量

记忆技巧：
- 默认值必须在编译时确定
- 用字面量或const常量
)" << endl;

    // --------------------------------------------
    // 4.4 错误4：默认参数与重载冲突
    // --------------------------------------------

    cout << "\n4.4 错误4：默认参数与重载冲突" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
void func(int a);
void func(int a, int b = 20);  // 可能导致二义性

调用：
func(10);  // 编译错误！不知道调用哪个

原因：
- func(10)可以匹配第1个函数
- 也可以匹配第2个函数（b使用默认值）
- 编译器无法决定

✅ 正确做法：
避免这种情况，要么：
1. 不用重载，只用默认参数
   void func(int a, int b = 20);

2. 或者重载时参数差异明显
   void func(int a);
   void func(int a, int b, int c);  // 参数个数差2个以上

记忆技巧：
- 默认参数和重载要小心配合
- 避免调用时产生二义性
)" << endl;

    // --------------------------------------------
    // 4.5 错误5：修改默认参数的值
    // --------------------------------------------

    cout << "\n4.5 错误5：修改默认参数的值" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 常见误解：
void func(int a = 10) {
    a = 20;  // 修改参数值
}

func();  // 下次调用，a还是10，不是20

原因：
- 默认值是编译时确定的
- 函数内修改参数不会影响默认值
- 每次调用都重新使用默认值

✅ 正确理解：
- 默认参数只是调用时的"占位符"
- 不是全局变量，不会保留修改

记忆技巧：
- 默认参数 = 编译时常量
- 每次调用都是新的副本
)" << endl;
}


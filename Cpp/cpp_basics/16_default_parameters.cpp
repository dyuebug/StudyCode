/*
【核心概念】默认参数（Default Parameters）

是什么？
- 在函数声明时为参数指定默认值
- 调用时如果不传该参数，就使用默认值

为什么需要？
- 让函数更灵活，可选参数
- 减少重载函数的数量
- 提供常用的默认行为

核心特点：
- 默认参数必须从右向左连续设置
- 调用时可以省略有默认值的参数
- 默认值在编译时确定
*/


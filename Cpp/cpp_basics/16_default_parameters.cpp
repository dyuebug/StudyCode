void demonstratePart5() {
    cout << string(60, '=') << endl;
    cout << "函数卡片速查" << endl;
    cout << string(60, '=') << endl;

    cout << R"(
【默认参数语法】

语法：返回类型 函数名(类型 参数1, 类型 参数2 = 默认值, ...)

规则：
1. 默认参数必须从右向左连续
2. 默认值只能在声明或定义中出现一次
3. 默认值必须是编译时常量

示例：
void func(int a, int b = 10, int c = 20);

调用：
func(1);        // a=1, b=10, c=20
func(1, 2);     // a=1, b=2, c=20
func(1, 2, 3);  // a=1, b=2, c=3

────────────────────────────────────

【默认参数 vs 函数重载】

默认参数：
- 优点：代码简洁，一个函数搞定
- 缺点：灵活性稍差，所有情况共用一个逻辑

函数重载：
- 优点：每个版本可以有不同的逻辑
- 缺点：代码量大，需要维护多个函数

选择建议：
- 参数有明显默认行为 → 用默认参数
- 不同参数需要不同逻辑 → 用重载
)" << endl;
}


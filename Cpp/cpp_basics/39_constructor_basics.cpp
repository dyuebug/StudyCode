/*
【核心概念】构造函数（Constructor）

是什么？
- 构造函数是类的特殊成员函数，用于初始化对象
- 对象创建时自动调用，无需手动调用

为什么需要？
- 自动初始化对象，避免使用未初始化的数据
- 简化对象创建过程
- 保证对象创建时处于有效状态

核心特点：
- 函数名与类名相同
- 没有返回类型（连void都不写）
- 可以重载（多个构造函数）
- 对象创建时自动调用
*/


#include <iostream>
#include <string>
using namespace std;

// --------------------------------------------
// 2.1 示例1：默认构造函数
// --------------------------------------------

// 定义一个点类
class Point {
private:
    int x;
    int y;

public:
    // 默认构造函数：无参数
    // 功能：将点初始化为原点(0, 0)
    Point() {
        x = 0;
        y = 0;
        cout << "调用默认构造函数：Point() - 创建点(" << x << ", " << y << ")" << endl;
    }

    // 显示点的坐标
    void display() {
        cout << "点的坐标：(" << x << ", " << y << ")" << endl;
    }
};

// --------------------------------------------
// 2.2 示例2：带参数的构造函数
// --------------------------------------------

// 定义一个矩形类
class Rectangle {
private:
    double width;
    double height;

public:
    // 带参数的构造函数
    // 参数：w - 宽度，h - 高度
    Rectangle(double w, double h) {
        width = w;
        height = h;
        cout << "调用带参数构造函数：Rectangle(" << w << ", " << h << ")" << endl;
    }

    // 计算面积
    double getArea() {
        return width * height;
    }

    // 显示信息
    void display() {
        cout << "矩形：宽=" << width << ", 高=" << height << ", 面积=" << getArea() << endl;
    }
};

// --------------------------------------------
// 2.3 示例3：构造函数重载
// --------------------------------------------

// 定义一个学生类，演示构造函数重载
class Student {
private:
    string name;
    int age;
    double score;

public:
    // 构造函数1：默认构造函数
    Student() {
        name = "未命名";
        age = 0;
        score = 0.0;
        cout << "调用构造函数1：Student()" << endl;
    }

    // 构造函数2：只初始化姓名
    Student(string n) {
        name = n;
        age = 0;
        score = 0.0;
        cout << "调用构造函数2：Student(string)" << endl;
    }

    // 构造函数3：初始化姓名和年龄
    Student(string n, int a) {
        name = n;
        age = a;
        score = 0.0;
        cout << "调用构造函数3：Student(string, int)" << endl;
    }

    // 构造函数4：初始化所有成员
    Student(string n, int a, double s) {
        name = n;
        age = a;
        score = s;
        cout << "调用构造函数4：Student(string, int, double)" << endl;
    }

    // 显示学生信息
    void display() {
        cout << "学生：" << name << ", 年龄：" << age << ", 成绩：" << score << endl;
    }
};

// --------------------------------------------
// 2.4 示例4：使用初始化列表
// --------------------------------------------

// 定义一个圆类，演示初始化列表
class Circle {
private:
    double radius;
    const double PI;  // const成员变量

public:
    // 使用初始化列表初始化成员变量
    // 语法：构造函数名(参数) : 成员1(值1), 成员2(值2) { }
    Circle(double r) : radius(r), PI(3.14159) {
        cout << "调用构造函数：Circle(" << r << ") - 使用初始化列表" << endl;
    }

    // 计算面积
    double getArea() {
        return PI * radius * radius;
    }

    // 显示信息
    void display() {
        cout << "圆：半径=" << radius << ", 面积=" << getArea() << endl;
    }
};


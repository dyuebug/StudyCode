#include <iostream>
#include <iomanip>  // 用于格式化输出
using namespace std;

int main() {
    cout << "=== 二维数组详解 ===" << endl << endl;

    // ========================================
    // 1. 什么是二维数组？
    // ========================================
    cout << "【1. 二维数组的概念】" << endl;
    cout << "二维数组可以理解为：" << endl;
    cout << "  - 表格（行和列）" << endl;
    cout << "  - 矩阵（数学中的矩阵）" << endl;
    cout << "  - 数组的数组（每一行是一个一维数组）" << endl << endl;

    // ========================================
    // 2. 二维数组的声明和初始化
    // ========================================
    cout << "【2. 二维数组的声明和初始化】" << endl;

    // 方式1：声明时完全初始化
    int matrix1[3][4] = {
        {1, 2, 3, 4},      // 第0行
        {5, 6, 7, 8},      // 第1行
        {9, 10, 11, 12}    // 第2行
    };
    cout << "方式1 - 完全初始化（3行4列）：" << endl;
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 4; j++) {
            cout << setw(4) << matrix1[i][j];  // setw(4)设置宽度为4
        }
        cout << endl;
    }
    cout << endl;

    // 方式2：省略内层大括号（按行顺序填充）
    int matrix2[2][3] = {1, 2, 3, 4, 5, 6};
    cout << "方式2 - 省略内层大括号（2行3列）：" << endl;
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 3; j++) {
            cout << setw(4) << matrix2[i][j];
        }
        cout << endl;
    }
    cout << endl;

    // 方式3：部分初始化（未初始化的元素自动为0）
    int matrix3[2][3] = {{1, 2}, {4}};
    cout << "方式3 - 部分初始化（未指定的元素为0）：" << endl;
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 3; j++) {
            cout << setw(4) << matrix3[i][j];
        }
        cout << endl;
    }
    cout << endl;

    // 方式4：先声明，后赋值
    int matrix4[2][2];
    matrix4[0][0] = 10;
    matrix4[0][1] = 20;
    matrix4[1][0] = 30;
    matrix4[1][1] = 40;
    cout << "方式4 - 先声明后赋值：" << endl;
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 2; j++) {
            cout << setw(4) << matrix4[i][j];
        }
        cout << endl;
    }
    cout << endl;

    // ========================================
    // 3. 二维数组的访问
    // ========================================
    cout << "【3. 二维数组的访问】" << endl;
    cout << "访问格式：数组名[行下标][列下标]" << endl;
    cout << "matrix1[0][0] = " << matrix1[0][0] << " (第0行第0列)" << endl;
    cout << "matrix1[1][2] = " << matrix1[1][2] << " (第1行第2列)" << endl;
    cout << "matrix1[2][3] = " << matrix1[2][3] << " (第2行第3列)" << endl;
    cout << endl;

    // ========================================
    // 4. 二维数组的遍历（嵌套循环）
    // ========================================
    cout << "【4. 二维数组的遍历】" << endl;
    int scores[3][4] = {
        {85, 90, 78, 92},  // 学生1的4门课成绩
        {88, 76, 95, 89},  // 学生2的4门课成绩
        {92, 88, 84, 90}   // 学生3的4门课成绩
    };

    cout << "学生成绩表：" << endl;
    cout << "学生\\课程  课程1  课程2  课程3  课程4" << endl;
    for (int i = 0; i < 3; i++) {
        cout << "学生" << (i + 1) << "     ";
        for (int j = 0; j < 4; j++) {
            cout << setw(6) << scores[i][j];
        }
        cout << endl;
    }
    cout << endl;

    // ========================================
    // 5. 实战案例1：计算每个学生的总分和平均分
    // ========================================
    cout << "【5. 实战案例1：统计学生成绩】" << endl;
    for (int i = 0; i < 3; i++) {
        int sum = 0;
        for (int j = 0; j < 4; j++) {
            sum += scores[i][j];
        }
        double average = sum / 4.0;
        cout << "学生" << (i + 1) << " - 总分：" << sum
             << "，平均分：" << fixed << setprecision(2) << average << endl;
    }
    cout << endl;

    // ========================================
    // 6. 实战案例2：计算每门课程的平均分
    // ========================================
    cout << "【6. 实战案例2：统计课程成绩】" << endl;
    for (int j = 0; j < 4; j++) {
        int sum = 0;
        for (int i = 0; i < 3; i++) {
            sum += scores[i][j];
        }
        double average = sum / 3.0;
        cout << "课程" << (j + 1) << " - 平均分："
             << fixed << setprecision(2) << average << endl;
    }
    cout << endl;

    // ========================================
    // 7. 实战案例3：矩阵转置
    // ========================================
    cout << "【7. 实战案例3：矩阵转置】" << endl;
    int original[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };
    int transposed[3][2];  // 转置后的矩阵（行列互换）

    // 执行转置
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 3; j++) {
            transposed[j][i] = original[i][j];
        }
    }

    cout << "原矩阵（2行3列）：" << endl;
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 3; j++) {
            cout << setw(4) << original[i][j];
        }
        cout << endl;
    }

    cout << "转置后（3行2列）：" << endl;
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 2; j++) {
            cout << setw(4) << transposed[i][j];
        }
        cout << endl;
    }
    cout << endl;

    // ========================================
    // 8. 内存布局（行优先存储）
    // ========================================
    cout << "【8. 二维数组的内存布局】" << endl;
    int arr[2][3] = {{1, 2, 3}, {4, 5, 6}};
    cout << "二维数组在内存中是连续存储的（行优先）：" << endl;
    cout << "arr[0][0] 地址：" << &arr[0][0] << endl;
    cout << "arr[0][1] 地址：" << &arr[0][1] << endl;
    cout << "arr[0][2] 地址：" << &arr[0][2] << endl;
    cout << "arr[1][0] 地址：" << &arr[1][0] << endl;
    cout << "arr[1][1] 地址：" << &arr[1][1] << endl;
    cout << "arr[1][2] 地址：" << &arr[1][2] << endl;
    cout << "可以看到地址是连续的，按行存储" << endl << endl;

    // ========================================
    // 9. 常见错误
    // ========================================
    cout << "【9. 常见错误】" << endl;
    cout << "❌ 错误1：下标越界" << endl;
    cout << "   int arr[2][3];  // 2行3列" << endl;
    cout << "   arr[2][0] = 10; // 错误！行下标只能是0或1" << endl;
    cout << "   arr[0][3] = 20; // 错误！列下标只能是0、1、2" << endl << endl;

    cout << "❌ 错误2：初始化时省略第二维大小" << endl;
    cout << "   int arr[][3] = {{1,2,3}, {4,5,6}}; // ✓ 正确" << endl;
    cout << "   int arr[2][] = {{1,2,3}, {4,5,6}}; // ✗ 错误！" << endl << endl;

    cout << "❌ 错误3：混淆行列" << endl;
    cout << "   int arr[3][4]; // 3行4列" << endl;
    cout << "   // 遍历时要注意：外层循环行(0-2)，内层循环列(0-3)" << endl << endl;

    // ========================================
    // 10. 学习总结
    // ========================================
    cout << "【10. 学习总结】" << endl;
    cout << "✓ 二维数组本质是\"数组的数组\"" << endl;
    cout << "✓ 声明格式：类型 数组名[行数][列数]" << endl;
    cout << "✓ 访问格式：数组名[行下标][列下标]" << endl;
    cout << "✓ 遍历使用嵌套循环：外层行，内层列" << endl;
    cout << "✓ 内存布局：行优先连续存储" << endl;
    cout << "✓ 应用场景：表格、矩阵、棋盘、图像等" << endl;

    return 0;
}

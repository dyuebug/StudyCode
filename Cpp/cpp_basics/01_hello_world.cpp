// --------------------------------------------
// 1.1 预处理指令：#include
// --------------------------------------------

/*
【#include 预处理指令】

是什么？
- #include 是预处理指令，用于包含头文件
- 头文件包含了我们需要使用的函数和对象的声明

为什么需要？
- iostream 是输入输出流库，包含了 cout（输出）和 cin（输入）
- 没有它，我们无法使用 cout 来输出信息

语法：
#include <头文件名>  // 包含系统头文件
#include "头文件名"  // 包含自定义头文件
*/

#include <iostream>  // 包含输入输出流库，提供 cout 和 cin
#include <cstdlib>   // 包含 system() 函数

// --------------------------------------------
// 1.2 命名空间：using namespace std
// --------------------------------------------

/*
【命名空间 namespace】

是什么？
- 命名空间是用来组织代码的，避免名称冲突
- std 是标准命名空间，包含了C++标准库的所有内容

为什么需要？
- cout、cin 等对象都在 std 命名空间中
- 如果不使用 using namespace std，需要写成 std::cout

两种写法：
1. using namespace std;  // 使用整个命名空间（初学者推荐）
2. std::cout             // 每次使用时加 std:: 前缀（更安全）
*/

using namespace std;  // 使用标准命名空间，这样就可以直接写 cout 而不是 std::cout

// --------------------------------------------
// 1.3 主函数：main()
// --------------------------------------------

/*
【main() 函数】

是什么？
- main() 是程序的入口点，程序从这里开始执行
- 每个C++程序必须有且只有一个 main() 函数

语法：
int main() {
    // 程序代码
    return 0;  // 返回0表示程序正常结束
}

返回值：
- int 表示返回整数类型
- return 0 表示程序成功执行
- return 非0值 表示程序出错
*/

int main() {
    // 这是主函数的开始
    
    system("chcp 65001");  // 设置控制台为UTF-8编码

    // ============================================
    // 第二部分：基础操作实践
    // ============================================

    // --------------------------------------------
    // 2.1 使用 cout 输出信息
    // --------------------------------------------

    // cout：console output（控制台输出）
    // <<：插入运算符，将右边的内容输出到左边的流中
    // "Hello, World!"：要输出的字符串，用双引号包围
    // endl：end line（结束行），相当于换行符

    cout << "Hello, World!" << endl;

    // 上面这行代码的执行过程：
    // 1. cout 是输出流对象
    // 2. << 将 "Hello, World!" 发送到 cout
    // 3. << 再将 endl（换行）发送到 cout
    // 4. 最终在控制台显示：Hello, World!（并换行）

    // --------------------------------------------
    // 2.2 多种输出方式
    // --------------------------------------------

    // 方式1：使用 endl 换行
    cout << "欢迎学习C++！" << endl;

    // 方式2：使用 \n 换行（更高效）
    cout << "这是第二行\n";

    // 方式3：一次输出多个内容
    cout << "我的名字是：" << "学习者" << "，年龄是：" << 20 << endl;

    // 方式4：连续输出
    cout << "第一部分 ";
    cout << "第二部分 ";
    cout << "第三部分" << endl;

    // --------------------------------------------
    // 2.3 输出不同类型的数据
    // --------------------------------------------

    cout << "整数：" << 42 << endl;           // 输出整数
    cout << "浮点数：" << 3.14159 << endl;    // 输出浮点数
    cout << "字符：" << 'A' << endl;          // 输出字符（单引号）
    cout << "布尔值：" << true << endl;       // 输出布尔值（1表示true）

    // ============================================
    // 第三部分：深入理解（可选）
    // ============================================

    /*
    ==============================================
    🔍 深入理解：为什么需要 return 0？（选学）
    ==============================================

    如果你想知道"为什么"，可以阅读这部分。
    如果觉得难，可以跳过，不影响后续学习。

    【深入内容】

    1. 返回值的作用：
       - main() 函数返回一个整数给操作系统
       - 0 表示程序正常结束
       - 非0值表示程序出错（不同的错误码代表不同的错误）

    2. 操作系统如何使用返回值：
       - 在命令行中，可以通过 $? (Linux/Mac) 或 %ERRORLEVEL% (Windows) 查看
       - 脚本可以根据返回值判断程序是否成功执行

    3. 可以省略吗？
       - C++标准规定，如果 main() 函数没有 return 语句，编译器会自动添加 return 0
       - 但为了代码清晰，建议显式写出 return 0

    ==============================================
    */

    // 返回0，表示程序正常结束
    return 0;
}


/*
常见错误1：忘记包含头文件
❌ 错误代码：
// #include <iostream>  // 忘记包含
int main() {
    cout << "Hello" << endl;  // 错误：cout 未定义
    return 0;
}

结果：编译错误，提示 'cout' was not declared in this scope

原因：cout 在 iostream 头文件中定义，不包含头文件就无法使用

✅ 正确代码：
#include <iostream>
int main() {
    std::cout << "Hello" << std::endl;
    return 0;
}

记忆技巧：使用 cout/cin 必须包含 <iostream>

────────────────────────────────────

常见错误2：忘记 using namespace std
❌ 错误代码：
#include <iostream>
// 忘记 using namespace std
int main() {
    cout << "Hello" << endl;  // 错误：cout 未定义
    return 0;
}

结果：编译错误，提示 'cout' was not declared in this scope

原因：cout 在 std 命名空间中，需要加 std:: 前缀或使用 using namespace std

✅ 正确代码（方式1）：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl;
    return 0;
}

✅ 正确代码（方式2）：
#include <iostream>
int main() {
    std::cout << "Hello" << std::endl;
    return 0;
}

记忆技巧：要么使用 using namespace std，要么每次写 std::

────────────────────────────────────

常见错误3：忘记分号
❌ 错误代码：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl  // 忘记分号
    return 0;
}

结果：编译错误，提示 expected ';' before 'return'

原因：C++中每条语句必须以分号结尾

✅ 正确代码：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl;  // 加上分号
    return 0;
}

记忆技巧：每条语句结尾必须有分号（除了预处理指令和函数定义）

────────────────────────────────────

常见错误4：main函数拼写错误
❌ 错误代码：
#include <iostream>
using namespace std;
int Main() {  // 错误：M大写了
    cout << "Hello" << endl;
    return 0;
}

结果：链接错误，提示 undefined reference to 'main'

原因：C++区分大小写，必须是小写的 main

✅ 正确代码：
#include <iostream>
using namespace std;
int main() {  // 正确：小写的 main
    cout << "Hello" << endl;
    return 0;
}

记忆技巧：main 函数名必须全部小写

────────────────────────────────────

常见错误5：字符串使用单引号
❌ 错误代码：
#include <iostream>
using namespace std;
int main() {
    cout << 'Hello' << endl;  // 错误：字符串用了单引号
    return 0;
}

结果：编译错误或警告，可能输出奇怪的数字

原因：单引号用于字符（单个字符），双引号用于字符串

✅ 正确代码：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl;  // 正确：字符串用双引号
    cout << 'A' << endl;      // 字符用单引号
    return 0;
}

记忆技巧：
- 单引号 ' ' 用于单个字符：'A', 'b', '1'
- 双引号 " " 用于字符串："Hello", "C++"

────────────────────────────────────

常见错误6：忘记 return 0
❌ 不推荐的代码：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl;
    // 忘记 return 0
}

结果：虽然能编译通过（编译器会自动添加），但不是好习惯

原因：虽然C++标准允许省略，但显式写出更清晰

✅ 正确代码：
#include <iostream>
using namespace std;
int main() {
    cout << "Hello" << endl;
    return 0;  // 显式返回0
}

记忆技巧：养成习惯，main函数结尾总是写 return 0

────────────────────────────────────

常见错误7：中文乱码问题
❌ 问题代码：
#include <iostream>
using namespace std;
int main() {
    cout << "你好，世界！" << endl;  // 可能显示乱码
    return 0;
}

结果：在某些系统上，中文显示为乱码

原因：文件编码不是UTF-8，或控制台不支持UTF-8

✅ 解决方法：
1. 保存文件时使用UTF-8编码
2. Windows系统可以在代码开头添加：
   system("chcp 65001");  // 设置控制台为UTF-8编码
3. 或者使用英文进行学习（推荐）

记忆技巧：初学阶段建议使用英文，避免编码问题
*/


void demonstratePart4() {
    cout << string(60, '=') << endl;
    cout << "常见错误和陷阱 ⭐⭐⭐⭐⭐" << endl;
    cout << string(60, '=') << endl;

    // --------------------------------------------
    // 4.1 错误1：数组越界（最常见！）
    // --------------------------------------------

    cout << "\n4.1 错误1：数组越界 ⚠️⚠️⚠️" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[5];  // 越界！数组只有arr[0]到arr[4]
arr[10] = 100;   // 严重越界！

结果：
- 可能输出随机值
- 可能修改其他变量的值
- 可能导致程序崩溃
- C++不会检查越界，非常危险！

原因：
- 数组下标范围是0到n-1
- arr[5]访问的是数组外的内存

✅ 正确代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[4];  // 访问最后一个元素
for (int i = 0; i < 5; i++) {  // 循环条件是 i < 5
    cout << arr[i];
}

记忆技巧：
- 数组大小是n，下标范围是0到n-1
- 循环条件用 i < n，不要用 i <= n
- 永远记住：下标从0开始！
)" << endl;

    // --------------------------------------------
    // 4.2 错误2：未初始化就使用
    // --------------------------------------------

    cout << "\n4.2 错误2：未初始化就使用" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr[5];  // 声明但未初始化
cout << arr[0];  // 输出随机值

结果：
- 数组元素包含随机的垃圾数据
- 每次运行结果可能不同

原因：
- C++不会自动初始化局部数组
- 内存中的旧数据被当作数组元素

✅ 正确代码：
int arr[5] = {};  // 全部初始化为0
int arr2[5] = {1, 2, 3, 4, 5};  // 显式初始化

记忆技巧：
- 声明数组时立即初始化
- 使用 = {} 可以全部初始化为0
)" << endl;

    // --------------------------------------------
    // 4.3 错误3：数组大小使用变量
    // --------------------------------------------

    cout << "\n4.3 错误3：数组大小使用变量" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int n = 5;
int arr[n];  // 编译错误！（C++标准不允许）

原因：
- C++标准要求数组大小必须是编译时常量
- 变量n的值在运行时才确定

✅ 正确代码：
// 方式1：使用常量
const int SIZE = 5;
int arr[SIZE];

// 方式2：使用字面量
int arr2[5];

// 方式3：使用动态数组（后续会学）
int n = 5;
int* arr3 = new int[n];  // 动态分配内存

记忆技巧：
- 数组大小必须是常量或字面量
- 需要动态大小时，使用动态内存分配
)" << endl;

    // --------------------------------------------
    // 4.4 错误4：数组赋值错误
    // --------------------------------------------

    cout << "\n4.4 错误4：数组赋值错误" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr1[5] = {1, 2, 3, 4, 5};
int arr2[5];
arr2 = arr1;  // 编译错误！数组不能直接赋值

原因：
- 数组名是常量指针，不能被赋值
- 需要逐个元素复制

✅ 正确代码：
int arr1[5] = {1, 2, 3, 4, 5};
int arr2[5];

// 方式1：使用循环复制
for (int i = 0; i < 5; i++) {
    arr2[i] = arr1[i];
}

// 方式2：使用标准库函数（后续会学）
#include <algorithm>
std::copy(arr1, arr1 + 5, arr2);

记忆技巧：
- 数组不能直接赋值
- 需要逐个元素复制
)" << endl;

    // --------------------------------------------
    // 4.5 错误5：混淆数组大小和最后一个下标
    // --------------------------------------------

    cout << "\n4.5 错误5：混淆数组大小和最后一个下标" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[5];  // 越界！

原因：
- 数组大小是5
- 但最后一个元素的下标是4（不是5）
- arr[5]已经越界

✅ 正确代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[4];  // 访问最后一个元素

记忆技巧：
- 数组大小 = 元素个数
- 最后一个下标 = 数组大小 - 1
- 口诀：大小是5，下标到4
)" << endl;

    // --------------------------------------------
    // 4.6 错误6：负数下标
    // --------------------------------------------

    cout << "\n4.6 错误6：负数下标" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[-1];  // 负数下标，越界！

结果：
- 访问数组前面的内存
- 可能读取其他变量的值
- 可能导致程序崩溃

原因：
- 下标必须是0到n-1的非负整数
- 负数下标会访问数组前面的内存

✅ 正确代码：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[0];  // 访问第1个元素

记忆技巧：
- 下标范围：0 到 n-1
- 永远不要使用负数下标
)" << endl;

    // --------------------------------------------
    // 4.7 错误7：循环条件错误
    // --------------------------------------------

    cout << "\n4.7 错误7：循环条件错误" << endl;
    cout << string(60, '-') << endl;

    cout << R"(
❌ 错误代码：
int arr[5] = {10, 20, 30, 40, 50};
for (int i = 0; i <= 5; i++) {  // 错误！i会到5
    cout << arr[i];  // arr[5]越界
}

原因：
- 循环条件是 i <= 5
- 当i=5时，arr[5]越界

✅ 正确代码：
int arr[5] = {10, 20, 30, 40, 50};
for (int i = 0; i < 5; i++) {  // 正确！i只到4
    cout << arr[i];
}

记忆技巧：
- 循环条件用 i < n（不是 i <= n）
- i的范围是0到n-1
)" << endl;
}


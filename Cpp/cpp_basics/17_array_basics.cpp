void demonstratePart2() {
    cout << string(60, '=') << endl;
    cout << "第二部分：基础操作实践" << endl;
    cout << string(60, '=') << endl;

    // --------------------------------------------
    // 2.1 示例1：声明和初始化数组
    // --------------------------------------------

    cout << "\n2.1 示例1：声明和初始化数组" << endl;
    cout << string(60, '-') << endl;

    // 方式1：声明后逐个赋值
    int arr1[3];      // 声明3个整数的数组
    arr1[0] = 10;     // 第1个元素赋值为10
    arr1[1] = 20;     // 第2个元素赋值为20
    arr1[2] = 30;     // 第3个元素赋值为30

    cout << "arr1[0] = " << arr1[0] << endl;
    cout << "arr1[1] = " << arr1[1] << endl;
    cout << "arr1[2] = " << arr1[2] << endl;

    // 方式2：声明时初始化
    int arr2[5] = {100, 200, 300, 400, 500};
    cout << "\narr2完全初始化：" << endl;
    cout << "arr2[0] = " << arr2[0] << endl;
    cout << "arr2[4] = " << arr2[4] << endl;

    // 方式3：部分初始化（剩余元素自动为0）
    int arr3[5] = {1, 2};  // 只初始化前2个元素
    cout << "\narr3部分初始化：" << endl;
    cout << "arr3[0] = " << arr3[0] << endl;  // 1
    cout << "arr3[1] = " << arr3[1] << endl;  // 2
    cout << "arr3[2] = " << arr3[2] << endl;  // 0（自动初始化）
    cout << "arr3[3] = " << arr3[3] << endl;  // 0
    cout << "arr3[4] = " << arr3[4] << endl;  // 0

    // 方式4：全部初始化为0
    int arr4[5] = {};  // 所有元素都是0
    cout << "\narr4全部初始化为0：" << endl;
    cout << "arr4[0] = " << arr4[0] << endl;  // 0
    cout << "arr4[4] = " << arr4[4] << endl;  // 0

    // 方式5：自动推断大小
    int arr5[] = {10, 20, 30};  // 编译器自动推断大小为3
    cout << "\narr5自动推断大小：" << endl;
    cout << "arr5[0] = " << arr5[0] << endl;
    cout << "arr5[1] = " << arr5[1] << endl;
    cout << "arr5[2] = " << arr5[2] << endl;

    // --------------------------------------------
    // 2.2 示例2：遍历数组
    // --------------------------------------------

    cout << "\n2.2 示例2：遍历数组" << endl;
    cout << string(60, '-') << endl;

    // 定义数组：存储5个学生的成绩
    int scores[5] = {85, 92, 78, 95, 88};

    // 方式1：使用for循环遍历
    cout << "方式1：使用for循环遍历" << endl;
    for (int i = 0; i < 5; i++) {
        // i：循环变量，从0到4
        // scores[i]：访问第i个元素
        cout << "第" << (i + 1) << "个学生的成绩：" << scores[i] << endl;
    }

    // 方式2：使用范围for循环（C++11）
    cout << "\n方式2：使用范围for循环" << endl;
    for (int score : scores) {
        // score：自动遍历数组中的每个元素
        cout << "成绩：" << score << endl;
    }

    // --------------------------------------------
    // 2.3 示例3：计算数组元素的和与平均值
    // --------------------------------------------

    cout << "\n2.3 示例3：计算数组元素的和与平均值" << endl;
    cout << string(60, '-') << endl;

    int numbers[5] = {10, 20, 30, 40, 50};
    int sum = 0;  // 存储总和

    // 遍历数组，累加每个元素
    for (int i = 0; i < 5; i++) {
        sum += numbers[i];  // 等价于：sum = sum + numbers[i]
    }

    // 计算平均值
    double average = sum / 5.0;  // 除以5.0得到浮点数结果

    cout << "数组元素：";
    for (int i = 0; i < 5; i++) {
        cout << numbers[i] << " ";
    }
    cout << endl;
    cout << "总和：" << sum << endl;
    cout << "平均值：" << average << endl;

    // --------------------------------------------
    // 2.4 示例4：查找数组中的最大值和最小值
    // --------------------------------------------

    cout << "\n2.4 示例4：查找数组中的最大值和最小值" << endl;
    cout << string(60, '-') << endl;

    int data[6] = {45, 23, 67, 12, 89, 34};

    // 假设第1个元素是最大值和最小值
    int max = data[0];
    int min = data[0];

    // 遍历数组，更新最大值和最小值
    for (int i = 1; i < 6; i++) {
        if (data[i] > max) {
            max = data[i];  // 找到更大的值，更新max
        }
        if (data[i] < min) {
            min = data[i];  // 找到更小的值，更新min
        }
    }

    cout << "数组元素：";
    for (int i = 0; i < 6; i++) {
        cout << data[i] << " ";
    }
    cout << endl;
    cout << "最大值：" << max << endl;
    cout << "最小值：" << min << endl;
}


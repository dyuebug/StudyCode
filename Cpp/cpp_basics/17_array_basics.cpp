/*
【语法1：声明数组】

类型 数组名[元素个数];

示例：
int scores[5];        // 声明5个整数的数组
double prices[10];    // 声明10个浮点数的数组
char letters[26];     // 声明26个字符的数组

注意：
- 元素个数必须是常量（不能是变量）
- 声明后，数组元素的值是未定义的（随机值）

【语法2：声明并初始化】

类型 数组名[元素个数] = {值1, 值2, ...};

示例：
int arr[5] = {10, 20, 30, 40, 50};  // 完全初始化
int arr2[5] = {1, 2};                // 部分初始化，剩余元素为0
int arr3[5] = {};                    // 全部初始化为0
int arr4[] = {1, 2, 3};              // 自动推断大小为3

【语法3：访问元素】

数组名[下标]

示例：
int arr[5] = {10, 20, 30, 40, 50};
cout << arr[0];  // 访问第1个元素：10
cout << arr[4];  // 访问第5个元素：50
arr[2] = 100;    // 修改第3个元素为100
*/


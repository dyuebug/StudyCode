/*
==============================================
🔍 深入理解：数组的内存布局（选学）
==============================================

如果你想知道"为什么"，可以阅读这部分。
如果觉得难，可以跳过，不影响后续学习。

【数组在内存中的存储】

1. 连续存储
   int arr[5] = {10, 20, 30, 40, 50};

   假设arr的起始地址是1000：
   arr[0] 地址：1000，值：10
   arr[1] 地址：1004，值：20（1000 + 4）
   arr[2] 地址：1008，值：30（1000 + 8）
   arr[3] 地址：1012，值：40（1000 + 12）
   arr[4] 地址：1016，值：50（1000 + 16）

   每个int占4字节，所以地址差4

2. 为什么下标从0开始？
   - 数组名arr代表起始地址
   - arr[i]的地址 = arr + i * sizeof(元素类型)
   - arr[0]的地址 = arr + 0 * 4 = arr（起始地址）
   - arr[1]的地址 = arr + 1 * 4 = arr + 4
   - 这样计算最简单，效率最高

3. 数组名的本质
   - 数组名是指向第1个元素的指针（后续会学）
   - arr 等价于 &arr[0]（arr[0]的地址）
   - 这就是为什么数组可以传递给函数

【数组 vs 变量】

变量：
int a = 10;
- 占用4字节内存
- 存储1个值

数组：
int arr[5] = {10, 20, 30, 40, 50};
- 占用20字节内存（5 * 4）
- 存储5个值
- 连续存储，访问快

【数组的优缺点】

优点：
- 访问速度快：O(1)时间复杂度
- 内存连续：缓存友好，性能好
- 简单直观：通过下标访问

缺点：
- 大小固定：声明后不能改变
- 越界危险：C++不检查下标范围
- 插入删除慢：需要移动大量元素

==============================================
*/


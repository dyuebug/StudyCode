/*
【数组】

是什么？
- 存储多个相同类型数据的容器
- 每个数据称为"元素"
- 通过"下标"访问元素

为什么需要？
- 问题：存储100个学生的成绩
- 不用数组：需要声明100个变量（score1, score2, ..., score100）
- 使用数组：只需声明一个数组（scores[100]）

内存布局：
假设有数组 int arr[5] = {10, 20, 30, 40, 50};

内存地址：  1000   1004   1008   1012   1016
数组元素：  [10]   [20]   [30]   [40]   [50]
下标：      arr[0] arr[1] arr[2] arr[3] arr[4]

特点：
- 连续存储：地址相邻（int占4字节，所以地址差4）
- 下标从0开始：arr[0]是第1个元素
- 固定大小：声明后不能改变大小
*/


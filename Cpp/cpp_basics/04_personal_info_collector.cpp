/*
【项目说明】

项目名称：个人信息收集器
项目目标：综合运用变量、数据类型、输入输出知识

功能要求：
1. 提示用户输入个人信息（姓名、年龄、身高、是否是学生）
2. 接收用户输入的各种类型数据
3. 格式化输出所有信息
4. 进行简单的数据处理和计算

学习目标：
- 掌握不同数据类型的使用
- 熟练使用 cin 和 cout
- 学会格式化输出
- 理解类型转换和计算
*/

#include <iostream>
#include <iomanip>   // 用于格式化输出
#include <string>    // 用于字符串类型
#include <cstdlib>
using namespace std;

int main() {
   system("chcp 65001");  // 设置控制台编码为 UTF-8，支持中文输出
    // ============================================
    // 第一部分：程序介绍
    // ============================================

    cout << string(60, '=') << endl;
    cout << "        个人信息收集器 v1.0" << endl;
    cout << string(60, '=') << endl;
    cout << "本程序将收集您的个人信息并进行展示" << endl;
    cout << string(60, '=') << endl;
    cout << endl;

    // ============================================
    // 第二部分：声明变量
    // ============================================

    // 声明用于存储用户信息的变量
    string name;           // 姓名（字符串类型）
    int age;               // 年龄（整数类型）
    double height;         // 身高，单位：米（浮点数类型）
    double weight;         // 体重，单位：千克（浮点数类型）
    char gender;           // 性别（字符类型：M/F）
    bool isStudent;        // 是否是学生（布尔类型）
    string city;           // 所在城市（字符串类型）

    // ============================================
    // 第三部分：收集用户信息
    // ============================================

    cout << "请输入您的个人信息：" << endl;
    cout << string(60, '-') << endl;

    // 1. 输入姓名（可能包含空格）
    cout << "姓名（可以包含空格）：";
    getline(cin, name);  // 使用 getline 读取整行，支持空格

    // 2. 输入年龄
    cout << "年龄（整数）：";
    cin >> age;

    // 3. 输入身高
    cout << "身高（米，例如1.75）：";
    cin >> height;

    // 4. 输入体重
    cout << "体重（千克，例如65.5）：";
    cin >> weight;

    // 5. 输入性别
    cout << "性别（M/F）：";
    cin >> gender;

    // 6. 输入是否是学生
    cout << "是否是学生（1=是，0=否）：";
    cin >> isStudent;

    // 清除输入缓冲区中的换行符
    // 因为前面使用了 cin >>，缓冲区中会留下换行符
    // 如果不清除，下一个 getline 会直接读取到换行符
    cin.ignore();

    // 7. 输入所在城市
    cout << "所在城市：";
    getline(cin, city);  // 使用 getline 读取整行

    // ============================================
    // 第四部分：数据处理和计算
    // ============================================

    // 计算BMI（体重指数）
    // BMI = 体重(kg) / 身高(m)²
    double bmi = weight / (height * height);

    // 判断年龄段
    string ageGroup;
    if (age < 18) {
        ageGroup = "未成年";
    } else if (age < 60) {
        ageGroup = "成年人";
    } else {
        ageGroup = "老年人";
    }

    // 判断BMI健康状况
    string bmiStatus;
    if (bmi < 18.5) {
        bmiStatus = "偏瘦";
    } else if (bmi < 24) {
        bmiStatus = "正常";
    } else if (bmi < 28) {
        bmiStatus = "偏胖";
    } else {
        bmiStatus = "肥胖";
    }

    // ============================================
    // 第五部分：格式化输出结果
    // ============================================

    cout << endl;
    cout << string(60, '=') << endl;
    cout << "        个人信息汇总" << endl;
    cout << string(60, '=') << endl;

    // 基本信息
    cout << "\n【基本信息】" << endl;
    cout << string(60, '-') << endl;
    cout << left;  // 设置左对齐
    cout << setw(20) << "姓名：" << name << endl;
    cout << setw(20) << "年龄：" << age << " 岁" << endl;
    cout << setw(20) << "年龄段：" << ageGroup << endl;
    cout << setw(20) << "性别：" << (gender == 'M' || gender == 'm' ? "男" : "女") << endl;
    cout << setw(20) << "是否是学生：" << (isStudent ? "是" : "否") << endl;
    cout << setw(20) << "所在城市：" << city << endl;

    // 身体信息
    cout << "\n【身体信息】" << endl;
    cout << string(60, '-') << endl;
    cout << fixed << setprecision(2);  // 固定显示2位小数
    cout << setw(20) << "身高：" << height << " 米" << endl;
    cout << setw(20) << "体重：" << weight << " 千克" << endl;
    cout << setw(20) << "BMI指数：" << bmi << endl;
    cout << setw(20) << "健康状况：" << bmiStatus << endl;

    // BMI参考标准
    cout << "\n【BMI参考标准】" << endl;
    cout << string(60, '-') << endl;
    cout << "偏瘦：BMI < 18.5" << endl;
    cout << "正常：18.5 ≤ BMI < 24" << endl;
    cout << "偏胖：24 ≤ BMI < 28" << endl;
    cout << "肥胖：BMI ≥ 28" << endl;

    // 统计信息
    cout << "\n【统计信息】" << endl;
    cout << string(60, '-') << endl;
    cout << "姓名长度：" << name.length() << " 个字符" << endl;
    cout << "年龄的10倍：" << age * 10 << endl;
    cout << "身高（厘米）：" << height * 100 << " cm" << endl;

    // ============================================
    // 第六部分：程序结束
    // ============================================

    cout << endl;
    cout << string(60, '=') << endl;
    cout << "感谢使用个人信息收集器！" << endl;
    cout << string(60, '=') << endl;

    return 0;
}


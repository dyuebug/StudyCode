/*
问题1：getline 读取不到内容
原因：前面使用了 cin >>，缓冲区中留有换行符
解决：在 getline 前使用 cin.ignore()

问题2：中文显示乱码
原因：文件编码不是UTF-8，或控制台不支持UTF-8
解决：
1. 保存文件时使用UTF-8编码
2. Windows系统在main函数开头添加：system("chcp 65001");

问题3：输入性别时不区分大小写
解决：使用条件判断 (gender == 'M' || gender == 'm')

问题4：BMI计算结果不准确
原因：可能是输入的身高单位不对（应该是米，不是厘米）
解决：在提示中明确说明单位

问题5：布尔值输入不方便
解决：可以改为输入字符（Y/N），然后转换为布尔值
*/

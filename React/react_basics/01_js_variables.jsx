// --------------------------------------------
// 2.1 使用const声明常量
// --------------------------------------------

// 声明一个常量：存储圆周率
// const表示这个值不会改变
const PI = 3.14159;

// 声明一个常量：存储网站名称
const SITE_NAME = "我的React学习网站";

// 声明一个常量：存储用户信息对象
// 注意：const不能修改引用，但可以修改对象属性
const user = {
  name: "张三",
  age: 25
};

// 输出常量
console.log("圆周率：", PI);
console.log("网站名称：", SITE_NAME);
console.log("用户信息：", user);

// 修改对象属性（允许）
user.age = 26;
console.log("修改后的年龄：", user.age);

// 尝试修改常量本身（会报错，注释掉避免程序中断）
// PI = 3.14;  // ❌ TypeError: Assignment to constant variable

console.log("=".repeat(60));

// --------------------------------------------
// 2.2 使用let声明可变变量
// --------------------------------------------

// 声明一个可变变量：计数器
// let表示这个值可能会改变
let count = 0;

// 修改变量的值
count = 1;
console.log("计数器：", count);

// 再次修改
count = count + 1;  // 或者写成 count += 1
console.log("计数器加1：", count);

// 声明一个可变变量：用户输入
let userInput = "";

// 模拟用户输入
userInput = "Hello";
console.log("用户输入：", userInput);

// 用户修改输入
userInput = "Hello World";
console.log("修改后的输入：", userInput);

console.log("=".repeat(60));

// --------------------------------------------
// 2.3 块级作用域演示
// --------------------------------------------

// 在外部声明变量
const outerName = "外部变量";
let outerCount = 10;

// 创建一个代码块（用{}包裹）
{
  // 在块内声明变量
  const innerName = "内部变量";
  let innerCount = 20;

  // 块内可以访问外部变量
  console.log("块内访问外部变量：", outerName);
  console.log("块内访问外部变量：", outerCount);

  // 块内可以访问内部变量
  console.log("块内访问内部变量：", innerName);
  console.log("块内访问内部变量：", innerCount);
}

// 块外可以访问外部变量
console.log("块外访问外部变量：", outerName);
console.log("块外访问外部变量：", outerCount);

// 块外不能访问内部变量（会报错，注释掉）
// console.log(innerName);  // ❌ ReferenceError: innerName is not defined

console.log("=".repeat(60));

// --------------------------------------------
// 2.4 var的问题演示（了解即可）
// --------------------------------------------

// 问题1：var没有块级作用域
{
  var varVariable = "var声明的变量";
}
console.log("块外访问var变量：", varVariable);  // ✅ 可以访问（这是个坑！）

// 问题2：var可以重复声明
var x = 10;
var x = 20;  // 不会报错，但容易出bug
console.log("重复声明的var：", x);

// 问题3：var会变量提升
console.log("变量提升：", y);  // undefined（不会报错，但很奇怪）
var y = 30;

console.log("=".repeat(60));

/*
==============================================
深入理解：为什么优先使用const？（选学）
==============================================

如果你想知道"为什么"，可以阅读这部分。
如果觉得难，可以跳过，不影响后续学习。

【深入内容】

1. 为什么React推荐使用const？
   - 不可变性（Immutability）是React的核心理念
   - const防止意外修改，减少bug
   - 代码更易理解（看到const就知道不会变）

2. const的"不可变"是什么意思？
   - 对于基本类型（数字、字符串）：值不能改变
   - 对于对象和数组：引用不能改变，但内容可以改变

   例子：
   ```jsx
   const num = 10;
   num = 20;  // ❌ 错误：不能修改基本类型的值

   const arr = [1, 2, 3];
   arr.push(4);  // ✅ 正确：可以修改数组内容
   arr = [5, 6];  // ❌ 错误：不能修改引用
   ```

3. 什么时候用let？
   - 循环计数器（for循环）
   - 需要重新赋值的变量
   - 条件判断中需要修改的变量

4. 为什么不用var？
   - 没有块级作用域，容易污染全局
   - 可以重复声明，容易出bug
   - 变量提升让代码难以理解
   - ES6之后，let和const完全可以替代var

==============================================
*/


/*
3.1 错误1：const声明后尝试修改

错误代码：
```jsx
const MAX_COUNT = 100;
MAX_COUNT = 200;  // ❌ TypeError: Assignment to constant variable
```

结果：程序报错，无法运行

原因：const声明的变量不能重新赋值

正确代码：
```jsx
let maxCount = 100;
maxCount = 200;  // ✅ 正确
```

记忆技巧：const = constant（常量），常量不能变
*/

/*
3.2 错误2：const声明时不初始化

错误代码：
```jsx
const name;  // ❌ SyntaxError: Missing initializer in const declaration
name = "Tom";
```

结果：语法错误，代码无法运行

原因：const必须在声明时就赋值

正确代码：
```jsx
const name = "Tom";  // ✅ 正确：声明时就赋值
```

记忆技巧：const声明的是常量，必须立即确定值
*/

/*
3.3 错误3：在声明前使用let/const变量

错误代码：
```jsx
console.log(count);  // ❌ ReferenceError: Cannot access 'count' before initialization
let count = 10;
```

结果：引用错误，无法访问

原因：let/const有"暂时性死区"，声明前不能使用

正确代码：
```jsx
let count = 10;
console.log(count);  // ✅ 正确：先声明再使用
```

记忆技巧：先声明，后使用
*/

/*
3.4 错误4：混淆const对象的"不可变"

错误理解：
```jsx
const user = { name: "Tom" };
user.name = "Jerry";  // 以为会报错
```

实际结果：不会报错，可以修改属性

原因：const只保证引用不变，不保证对象内容不变

正确理解：
```jsx
const user = { name: "Tom" };
user.name = "Jerry";  // ✅ 可以修改属性
user = { name: "Jerry" };  // ❌ 不能修改引用
```

记忆技巧：const锁定的是"盒子"，不是"盒子里的东西"
*/

/*
3.5 错误5：在块外访问块内变量

错误代码：
```jsx
{
  let message = "Hello";
}
console.log(message);  // ❌ ReferenceError: message is not defined
```

结果：引用错误，变量不存在

原因：let/const是块级作用域，只在{}内有效

正确代码：
```jsx
let message = "Hello";  // 在外部声明
{
  console.log(message);  // ✅ 可以访问外部变量
}
console.log(message);  // ✅ 可以访问
```

记忆技巧：变量的"活动范围"是它所在的{}
*/

/*
3.6 错误6：使用var导致的作用域问题

错误代码：
```jsx
for (var i = 0; i < 3; i++) {
  setTimeout(() => console.log(i), 100);
}
// 输出：3 3 3（预期是0 1 2）
```

结果：输出不符合预期

原因：var没有块级作用域，循环结束后i是3

正确代码：
```jsx
for (let i = 0; i < 3; i++) {
  setTimeout(() => console.log(i), 100);
}
// 输出：0 1 2
```

记忆技巧：循环用let，不用var
*/

/*
3.7 错误7：变量名使用保留字

错误代码：
```jsx
const class = "Math";  // ❌ SyntaxError: Unexpected token 'class'
const function = "test";  // ❌ SyntaxError: Unexpected token 'function'
```

结果：语法错误

原因：class、function等是JavaScript保留字

正确代码：
```jsx
const className = "Math";  // ✅ 正确
const functionName = "test";  // ✅ 正确
```

记忆技巧：变量名不能用JavaScript关键字
*/


/*
【命名规范】

1. 驼峰命名法（camelCase）
   - 第一个单词小写，后续单词首字母大写
   - 例子：userName, userAge, isLoggedIn

2. 常量用大写+下划线
   - 例子：MAX_COUNT, API_URL, DEFAULT_TIMEOUT

3. 变量名要有意义
   - ✅ 好：userName, totalPrice, isVisible
   - ❌ 差：x, data, temp

4. 布尔值用is/has/can开头
   - 例子：isActive, hasPermission, canEdit

5. 避免使用保留字
   - 不能用：class, function, return, if, for等
*/

// 示例：好的命名

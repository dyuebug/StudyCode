/*
【概念1：什么是变量？】

是什么？
- 变量是内存中的一个存储位置，有名字和值
- 就像一个贴了标签的盒子，可以放东西进去

为什么需要？
- 程序需要记住数据（比如用户的名字、年龄）
- 需要对数据进行计算和操作

核心特点：
- 有名字（标识符）
- 有值（可以是任何数据类型）
- 可以被读取和修改（取决于声明方式）
*/

// --------------------------------------------
// 1.1 三种声明方式对比
// --------------------------------------------

/*
【API卡片】

名称：let
作用：声明一个可以修改的变量
类型：ES6新增关键字

完整语法：
let 变量名 = 初始值;

特点：
1. 块级作用域（只在{}内有效）
2. 可以修改值
3. 不能重复声明
4. 不会变量提升（声明前不能使用）

使用示例：
```jsx
let count = 0;        // 声明并初始化
count = 1;            // 可以修改
let count = 2;        // ❌ 错误：不能重复声明
```

注意事项：
- 优先使用const，只有需要修改时才用let
- 变量名要有意义（count比x好）
*/

/*
【API卡片】

名称：const
作用：声明一个不能修改的常量
类型：ES6新增关键字

完整语法：
const 变量名 = 初始值;

特点：
1. 块级作用域（只在{}内有效）
2. 不能修改值（对于对象和数组，不能修改引用，但可以修改内容）
3. 必须在声明时初始化
4. 不能重复声明

使用示例：
```jsx
const PI = 3.14159;   // 声明常量
PI = 3.14;            // ❌ 错误：不能修改

const user = { name: 'Tom' };
user.name = 'Jerry';  // ✅ 正确：可以修改对象属性
user = {};            // ❌ 错误：不能修改引用
```

注意事项：
- 现代开发优先使用const
- 防止意外修改，代码更安全
*/

/*
【API卡片】

名称：var
作用：声明变量（旧语法，不推荐）
类型：ES5及之前的关键字

完整语法：
var 变量名 = 初始值;

特点：
1. 函数作用域（不是块级作用域）
2. 可以修改值
3. 可以重复声明
4. 会变量提升（声明前可以使用，值为undefined）

使用示例：
```jsx
var age = 18;
var age = 20;         // ✅ 可以重复声明（但容易出错）
console.log(x);       // undefined（变量提升）
var x = 10;
```

注意事项：
- 不推荐使用，有很多坑
- 只在维护旧代码时会遇到
*/


/*
【核心概念】变量声明

是什么？
- 变量是用来存储数据的"容器"，就像给数据起个名字

为什么需要？
- 程序需要记住和操作数据（用户输入、计算结果等）
- 变量让代码更易读、易维护

核心特点？
- JavaScript有3种声明方式：let、const、var
- 现代开发优先使用const，需要修改时用let
- var是旧语法，有很多坑，不推荐使用
*/

// ============================================
// 第一部分：核心概念详解

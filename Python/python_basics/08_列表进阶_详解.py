print("""
【引用 vs 拷贝】

引用（= 赋值）：
  b = a  → a和b指向同一个列表，改一个另一个也变

浅拷贝（copy/切片）：
  b = a.copy()  → 创建新列表，但内层嵌套的列表仍然共享

什么时候用哪个？
  - 普通列表（没有嵌套）：用 .copy() 或 [:] 就够了
  - 嵌套列表：需要用 copy.deepcopy()（后面会学）
""")

# 演示1：引用（危险！）
print("--- 引用（= 赋值）---")
original = [1, 2, 3, 4, 5]
reference = original  # 引用！不是拷贝！
reference.append(6)
print(f"  original：{original}")    # [1, 2, 3, 4, 5, 6] ← 也变了！
print(f"  reference：{reference}")  # [1, 2, 3, 4, 5, 6]
print(f"  是同一个对象？{original is reference}")  # True

# 演示2：浅拷贝（安全）
print("\n--- 浅拷贝（.copy()）---")
original = [1, 2, 3, 4, 5]
copy1 = original.copy()   # 方法1
copy2 = original[:]       # 方法2
copy3 = list(original)    # 方法3

copy1.append(6)
print(f"  original：{original}")  # [1, 2, 3, 4, 5] ← 不变！
print(f"  copy1：{copy1}")        # [1, 2, 3, 4, 5, 6]
print(f"  是同一个对象？{original is copy1}")  # False

# 演示3：浅拷贝对嵌套列表的局限
print("\n--- 浅拷贝的局限（嵌套列表）---")
nested = [[1, 2], [3, 4]]
shallow = nested.copy()
shallow[0][0] = 999  # 修改内层列表
print(f"  nested：{nested}")    # [[999, 2], [3, 4]] ← 内层也变了！
print(f"  shallow：{shallow}")  # [[999, 2], [3, 4]]

print("""
⚠️ 浅拷贝只复制了外层列表，内层列表仍然是共享的！
如果需要完全独立的副本，需要用 copy.deepcopy()：

  import copy
  deep = copy.deepcopy(nested)  # 完全独立的副本
""")

# 演示深拷贝
import copy
nested = [[1, 2], [3, 4]]
deep = copy.deepcopy(nested)
deep[0][0] = 999
print(f"  deepcopy后：nested = {nested}")  # [[1, 2], [3, 4]] ← 不变！
print(f"  deepcopy后：deep = {deep}")      # [[999, 2], [3, 4]]


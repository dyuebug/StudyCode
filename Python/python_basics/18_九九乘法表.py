# 外层循环：控制行（1-9）
# range(1, 10)生成1, 2, 3, ..., 9
for row in range(1, 10):
    # 内层循环：控制列（1到当前行号）
    # range(1, row+1)生成1到row
    # 例如：row=3时，range(1, 4)生成1, 2, 3
    for col in range(1, row + 1):
        # 计算结果
        result = col * row
        # 打印式子：列*行=结果
        # end="\t"：用制表符分隔，不换行
        print(f"{col}*{row}={result}", end="\t")

    # 内层循环结束后，换行
    print()


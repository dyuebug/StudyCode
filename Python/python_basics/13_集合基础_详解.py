# 运算符：|
all_students = class_a | class_b
print("并集（两个班所有人）：", all_students)

# 差集：只在第一个集合中、不在第二个集合中的元素
# 运算符：-
only_a = class_a - class_b
print("差集（只在A班的人）：", only_a)

# 对称差集：只在其中一个集合中，不在共同部分中的元素
# 运算符：^
diff_students = class_a ^ class_b
print("对称差集（只在其中一个班的人）：", diff_students)

